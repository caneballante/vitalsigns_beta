								<div class="pinkBarHeader">What is this indicator? </div>
								<p>Chinook salmon population abundance as measured by the number of natural origin adult fish returning to spawn.</p>
							
										<p>Chinook population abundance is reported here two ways: The number of natural origin spawners and the number of natural origin recruits. </p>
										<p>Spawning escapement is the number of Chinook salmon returning to rivers to spawn. Spawner abundance is normally estimated each year by counting the number of redds (gravel nests) in a river. Redds are counted by walking the stream and/or from boats or aircraft. For many populations, some hatchery-origin salmon mix with natural-origin salmon on the spawning grounds, complicating our estimates of natural-origin salmon returns. The proportion of spawners that were natural or hatchery origin is typically estimated based on the composition of carcasses and then extrapolated back to the total spawning population to estimate the number of natural-origin versus hatchery-origin spawners. </p>
										<p>The second measure, the total number of natural-origin recruits, represents the potential return of adult salmon to the spawning rivers if there were no commercial, subsistence, or recreational harvest affecting them. This is the estimated total number of natural-origin adult salmon before human harvest. The numbers of hatchery-origin fish were excluded from both estimates of Chinook abundance.</p>
								
								<!--*********************************PINK HEADER*********************************-->
								<div class="pinkBarHeader">What are the targets? </div>
								<h5>2020 target</h5>
								<p>Stop the overall decline and start seeing improvements in wild Chinook abundance in two to four populations in each biogeographic region (Figure 2 in Latest data and maps section).</p>

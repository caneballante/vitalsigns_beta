						<h3><a href="#">What You Can Do</a></h3>
						<div>
								<ol>
										<li>Manage manure: Collect, cover and compost. If you keep livestock, follow manure management practices. Your<a href="http://www.scc.wa.gov/index.php/contact/Conservation-Districts/"> local Conservation District</a> can provide you free technical assistance and will work with property owners to develop a waste management plan. </li>
										<li>Inspect and maintain your on-site sewage system – see see Septic System Care at <a href="http://www.pugetsoundstartshere.org/septic/index.php">Puget Sound Starts Here</a>.</li>
										<li><a href="www.scooppoop.org">Pick up dog poop</a> and put it in the trash</li>
										<li>Use <a href="http://www.skagitcounty.net/Common/Asp/Default.asp?d=PublicWorksCleanWater&amp;c=General&amp;p=DogCleanUp.htm">porta potties</a> when near rivers.</li>
										<li>Plant and maintain native vegetation around your property. For more information on native plants, visit<a href="http://www.ecy.wa.gov/programs/sea/pugetsound/species/native.html"> Dept. of Ecology's website</a> or the <a href="http://www.wnps.org/index.html">Washington Native Plant Society</a>. </li>
										<li>Enjoy eating Puget Sound shellfish safely by consulting <a href="http://ww4.doh.wa.gov/gis/mogifs/biotoxin.htm">Washington Department of Health</a> or <a href="http://ww4.doh.wa.gov/scripts/esrimap.dll?name=bioview&amp;Cmd=Map&amp;Step=1">local shellfish advisories</a>. </li>
								</ol>

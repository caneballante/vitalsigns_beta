						<h3><a href="#">Indicators and Targets</a></h3>
						<div>
								<div class="greenBarHeader">What is this indicator? </div>
								<p>The “acres of harvestable shellfish beds” indicator refers to those shellfish harvest areas that have been upgraded. </p>
								<div id="showMore1"><a href="#!" class="showMoreLink" data-attr="1">SHOW MORE</a></div>
								<div id="showLess1"><a href="#!" class="showLessLink" data-attr="1">SHOW LESS</a></div>
								<div id="morePara1">
										<p>DOH classifies 91 different shellfish growing areas in Puget Sound, covering roughly 190,000 acres. Sites are classified as “approved,” “conditionally approved,” “restricted,” or “prohibited” (Table 1). Upgrades in classification mean that water quality has improved, allowing for fewer restrictions on shellfish harvest. Downgrades mean there are either more restrictions on when shellfish may be harvested or no harvest is allowed at any time. Downgrades are generally caused by fecal bacteria or other pollutants in the water that make the shellfish unsafe to eat. </p>
										<p>The shellfish harvest area classification process is defined in federal rules and adopted in state regulations. The Department of Health (DOH) implements the rules at the state level. The purpose of the DOH program is to assure that harvested shellfish are safe to consume. This also includes making certain that pollution sources are continually assessed and marine water quality monitored around every classified harvest area. The data collected for the classification process not only represent the conditions that dictate shellfish harvest, but their trends can also indicate a healthier Puget Sound.</p>
										<p>DOH samples over 1,200 marine water stations between six and 12 times each year for fecal coliform bacteria, salinity, and temperature. Between 2.5 to five years of bacteria sampling data are used in the classification of each marine water station. In addition, shoreline pollution sources, including wastewater treatment plants, individual on-site sewage systems, marinas, farms, and any other activity with the potential to impact the shellfish area, are evaluated periodically and results are integrated in the classification process. </p>
										<img src="images/Shellfish_table_2013.jpg" width="800" height="324" /> </div>
								<div class="greenBarHeader">What are the targets?</div>
								<p>2020 Target: A net increase of 10,800 harvestable shellfish acres, including 7,000 acres where harvest had been prohibited, between 2007 and 2020.</p>
								<img src="images/shellfish_target_2013.png" width="900" height="205" />
								<div class="greenBarHeader">Is there progress?</div>
									
								<p>Yes. The 2020 target has not been reached yet, but there has been some progress.</p>
								<div id="showMore2"><a href="#!" class="showMoreLink" data-attr="2">SHOW MORE</a></div>
								<div id="showLess2"><a href="#!" class="showLessLink" data-attr="2">SHOW LESS</a></div>
								<div id="morePara2">
										<p>Shellfish beds are considered harvestable when their status is upgraded. Between 2007 and 2011, more acres of shellfish beds were upgraded than downgraded across all classifications, resulting in a net increase of 1,384 acres of harvestable shellfish beds. A net 3,290 acres of shellfish beds were upgraded from the prohibited classification (3,437 acres upgraded minus 147 acres downgraded to prohibited). </p>
										<p>However, these upgrades in growing area classifications from 2007 through 2011 were dramatically offset by the recent downgrade of the Samish Bay shellfish growing area (4,037 acres), impacting the overall net acreage gained since 2007 and slowing progress toward the 2020 goal.</p>

						<h3><a href="#">What Our Partners Are Doing</a></h3>
						<div>
								<ul>
										<li>Permittees Washington State Department of Transportaiton (WSDOT), municipalities, industries and project proponents) are working to implement their National Pollution Discharge Elimination System (NPDES) stormwater permits.</li>
										<li> Stormwater Outreach Regional Municipalities (STORM), Puget Sound Starts Here (PSSH) and others educate Puget Sound residents about a number of topics related to stormwater runoff and management. </li>
										<li> <a href="http://www.dnr.wa.gov/BusinessPermits/Topics/ShellfishAquaticLeasing/Pages/aqr_aqua_leasing_aquaculture.aspx">DNR's Aquaculture Leasing Program</a> generates revenue that supports the local economy, pays for management and restoration of state aquatic lands, and provides nationally recognized abundant shellfish.</li>
										<li> Through their <a href="http://www.dnr.wa.gov/BusinessPermits/Topics/ShellfishAquaticLeasing/Pages/aqr_wildstock_geoduck_fishery.aspx#wild">Wildstock Geoduck Fishery</a>, DNR manages the submerged aquatic lands in which the wild geoduck grow and offers right for private&nbsp;businesses to harvest specific quantities from site-specific areas. </li>
										<li>The directors of the Washington State Departments of Agriculture and Ecology and the Conservation Commission have initiated a process to more efficiently and effectively address water quality on agricultural lands</li>
										<li>Local and state agencies along with area property owners and other stakeholders are working to identify and correct water pollution issues in Samish Bay.</li>
										<li>The Washington State Department of Health is assuring that pollution identification and correction projects are started in all shellfish harvesting areas with threathened water quality.</li>
										<li>Programs to find and fix failing on-site sewage systems are underway in 19 Marine Recovery Areas within 9 Puget Sound counties.</li>
										<li>Over $2-million in Environmental Protection Agency (EPA) funds will be distributed to counties and tribes to develop sustainable pollution identification and correction programs.</li>
								</ul>

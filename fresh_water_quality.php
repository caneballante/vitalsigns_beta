						<h3><a href="#!">What you can do</a></h3>
						<div>
								<ol>
										<li>Use Low Impact Development (LID) techniques to manage <a href="http://www.psp.wa.gov/stormwater.php">stormwater runoff</a> such as natural landscaping, <a href="http://raingarden.wsu.edu/">rain gardens</a>, rain barrels, green roofs and permeable paving where appropriate.</li>
										<li>Let the Rain Soak In: Slow stormwater runoff by directing downspouts into lawns, beds or <a href="http://raingarden.wsu.edu/">rain gardens</a>. Plant more trees to help capture rainwater.</li>
										<li>Do not use pesticides on your lawn. Pesticides are poisoning some urban streams.</li>
										<li><a href="http://pugetsoundstartshere.org/in-your-local-area/get-involved/">Volunteer in your area</a> to work with a group helping to reduce fresh water pollution.</li>
										<li><a href="http://www.wsdot.wa.gov/choices/">Debris and pollution from cars affect water quality. Try <strong>alternatives to driving</strong> such as public transportation, carpooling, vanpooling, walking, or biking.</a></li>
										<li>If you do have a car, wash your car at a commercial car wash so wastewater plants can treat the water before it enters local waterways. Learn more at Puget Sound Starts here. </li>
										<li>Dispose of oil and other auto wastes at your local <a href="https://fortress.wa.gov/ecy/hwfacilitysearch/Forms/SiteSearch.aspx">recycling location or hazardous waste collection facility</a>. Some county-based links are available <a href="county_hazard_waste.php">here</a>.</li>

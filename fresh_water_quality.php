						<h3><a href="#!">Importance to Puget Sound recovery</a></h3>
						<div>
								<p>The rivers and streams that flow into Puget Sound are the lifeblood of our region’s ecosystems and our health, economy, and quality of life. Yet only 64% of the major rivers in Puget Sound meet water quality goals. </p>
								<p>Clean water is vital to people and to healthy fish and wildlife populations. When our rivers and streams pick up pollutants, toxic contaminants, or excessive sediments and nutrients, it adversely affects the health of our watersheds, marine waters, swimming beaches, and shellfish beds.</p>
								<p>Three key indicators help us monitor the health of Puget Sound: the number of impaired waters, the Water Quality Index, and the Benthic Index of Biotic Integrity. Under the federal Clean Water Act of 1972, waters that fail to meet water quality standards are considered impaired. The Water Quality Index integrates complex water quality data into a readily understood scale. The Benthic Index of Biotic Integrity measures the abundance and diversity of macroinvertebrates in a streambed. Also known as stream bugs, these creatures are a critical part of the aquatic food web and are sensitive to changes in the environment.</p>
						</div>
						
					
						

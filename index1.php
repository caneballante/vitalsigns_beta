						<h3><a href="#">About the Vital Signs</a></h3>
						<div>
								<h5>Puget Sound Partnership Dashboard of Vital Signs: <br />
										Charting our Course and Measuring Our Progress </h5>
								<p> Puget Sound, while one of the most vibrant and complex regions in the nation, is at the same time one of the most fragile ecosystems in the world. It is a place of beauty and wonder that we want to protect now and for generations to come. The Sound covers some 2,500 square miles, larger than the states of Delaware or Rhode Island, and is the drainage basin for more than 16,000 square miles. The Sound includes thousands of rivers and streams, rich forests, numerous plantlife, and numerous wildlife species including the iconic wild salmon and Orca whale. </p>
								<h5>Our actions, past and present have taken their toll</h5>
								<p>However, our region faces many severe challenges. Although the Sound looks beautiful, many past actions have taken their toll on the Sound&rsquo;s health: historic industrial and municipal pollutant discharges, near-shore land converted to residential and commercial uses, rainwater run-off from the land, and bulkheads built along our shores. Many swimming beaches and shellfish beds are closed because of contamination. Dead zones are appearing in our waterways where nothing can live because of the lack of oxygen. Populations of salmon and other species that once numbered in the millions have been reduced to the status of endangered species. These changes in our ecosystem have also impacted the tribal nations along our shores that depend on these resources to sustain their culture, traditions and way of life. Moreover, our population is expected to grow from 3.5 million to 5 million by 2025, adding additional challenges to what has occurred in the past. We need to change our behaviour so that we don't make the same mistakes.</p>
								<h5>Other challenges</h5>
								<p>These challenges are not merely aesthetic.&nbsp; They are vital to our region's survival and way of life. A healthy Puget Sound supports $20 billion in annual economic activity and hundreds of thousands of jobs. Puget Sound is one of the most popular venues for recreation regionally, and accounts for more than 80 percent of the state's tourism dollars. It also provides a sense of place and history for the people who live here.</p>
								<h5>The role of the Puget Sound Partnership</h5>
								<p>The Puget Sound Partnership was created in 2007 to serve as an advocate for the Sound, convening all of its public and private partners to focus our efforts on cleaning up the Sound, monitoring our effectiveness, and holding us accountable for our action and inaction. The Partnership adopted an Action Agenda – a comprehensive list of strategies and actions to drive the changes that are needed. An update of the Agenda is occurring this year and will inform what remains to be accomplished and help us set priorities. </p>
								<h5>Puget Sound Dashboard of Vital Signs</h5>
								<p>In 2010, the Puget Sound Partnership engaged our regional experts and the public in identifying the key ecosystem indicators and pressures that would help us identify whether we were making progress in restoring the Sound. In 2011, the Leadership Council adopted targets – specific measures that we could use as 'Vital Signs' of Puget Sound's health. The indicators and targets have been incorporated into a Dashboard that is intended to help us track our efforts. The Dashboard will serve as a report card on our success in meeting our targets. </p>
								<p> Each section of the Dashboard provides information on these indicators. It also provides an ongoing list of the efforts our Partners are making and suggestions on what individual citizens can do to join this effort. All of this information will be continuously updated. We have also provided a link to 'My Puget Sound' that will enable visitors to this site to engage friends and associates, provide additional strategies for restoring the Sound, or give us feedback on how our Dashboard can be improved.&nbsp; </p>
								<p>We hope the vital signs for the Sound on our Dashboard remain positive. Together, we can make a difference in our own lives and for future generations. <br />

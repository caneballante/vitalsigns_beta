				<div class="blueBarHeader">What is the indicator? </div>
				<h4 class="blue">This indicator is the annual summer low flow levels in Puget Sound rivers and streams.</h4>
				<p>
			    Low flow occurs during summer months when there is less rain and warmer temperatures. Summer low flow is measured as the minimum 30-day average water flow at river and stream gaging stations maintained by the U.S. Geological Survey from July through October.</p>
			    <p>The summer low flow indicator measures trends over a long time period. The indicator tracks how flow conditions are changing over the years, rather than comparing flow levels to a fixed value. The indicator is not sensitive to changes over a shorter time period, which makes it difficult to measure improving trends by 2020, even if significant flow restoration occurs. To measure a change, either large changes in flow must occur, such as a dam setting minimum downstream flows, or a very consistent change over a long period of time are needed.</p>
			    <p>The indicator tests whether the long-term trends of annual summer low flow levels are declining, increasing, or stable (no trend). The trend test uses data collected since 1975, representing over 40 years of measurements. The advantage of a long-term data set is that the influence of climate changes associated with regional cooling and warming cycles, such as the phases of the Pacific Decadal Oscillation, are minimized over time. </p>
			    <p>Specific targets were created for 13 rivers in the 2020 Action Agenda. The target language makes a distinction between regulated rivers with there is one or more dam on the river and unregulated rivers. Rivers with large dams have highly managed flows and specified minimum mitigation flows that can have a large influence on the trends in summer low flows.</p>


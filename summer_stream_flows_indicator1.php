                <p> In general, flows were roughly about average in most Puget Sound rivers and creeks in 2016. Exceptions were the Skagit River at Marblemount, North Fork Stillaguamish, and Mercer Creek, which were in the lowest quartile of historic summer low flows.</p>
                  <p>Of the stations assessed in 2016, 55% had stable or increasing summer low flows (16 out of 29). The remaining 13 rivers have either weakly or strongly decreasing flows. Given the importance of adequate water supply for salmon, other wildlife and humans, some areas in Puget Sound may be cause for concern.</p>
              <p>Rivers regulated by dams with mandatory minimum downstream flows generally were stable or showed increasing trends (Skagit, Cedar, Green, Puyallup, Nisqually, and Skokomish Rivers). Some of the glacier-fed upper tributaries had increasing trends (North Fork Nooksack River, Puyallup River at Orting, Nisqually River near National). This could be the result of climatic warming trends and glacial recession.</p>
				<p>The Cedar River is another regulated river. The gaging station near Landsburg, located immediately below the reservoir but above the City of Seattle water diversion, had a weak decreasing trend, while the Cedar River at Renton (near the mouth of the river) showed a strong increasing trend. Summer low flows upstream were about twice the low flows downstream. The Cedar River is considered important habitat for salmon and these patterns suggest that the implementation of the City of Seattle Habitat Conservation Plan is improving summer low flows.</p>
                  <p>Despite the thirteen rivers with either strongly or weakly decreasing flows meeting their interim targets, there remains cause for concern. Some of these water bodies, like Issaquah and Mercer Creeks are in urban areas. Others, namely the North Fork Stillaguamish, South Fork Snoqualmie, and Raging Rivers, are in areas experiencing population growth. The effect of increased impervious surfaces and ground and surface water withdrawals could be affecting those summer low flow levels. The Deschutes River gaging station is mostly forested land, so decreasing summer low flows in the upper Deschutes River may be due to a combination of forest practices and climate change. </p>
                  <p>The Dungeness River showed a weak increasing trend for the upper watershed. Current work to restore flows in the developed areas of the lower watershed is occurring downstream of this gage.</p>


										<p>The 2020 target is limited to 12 rivers. To provide a more complete regional picture, 17 additional gages were also evaluated for the period 1975-2014. Of the 29 gages used to measure summer low flow (shown in figure above):</p>
										<ul>
												<li>Sixteen gages are located near the mouth of major rivers or small streams that drain directly to Puget Sound. </li>
												<li>Four gages are from upstream sites on the mainstem of major rivers.</li>
												<li>Nine gages are from tributaries to major rivers.										</li>
										</ul>
										<p>Of the stations assessed, 66% had stable or increasing summer low flows (19 out of 29). The remaining 10 rivers have either weakly or strongly decreasing flows. Given the importance of adequate water supply for salmon, other wildlife and humans, some areas in Puget Sound may be cause for concern.</p>
										<p>Rivers regulated by dams with mandatory minimum downstream flows generally were stable or showed increasing trends (Skagit, Cedar, Green, Puyallup, Nisqually, and Skokomish rivers). Some of the glacier-fed upper tributaries had increasing trends (North Fork Nooksack River, Puyallup River at Orting, Nisqually River near National). This could be the result of climatic warming trends and glacial recession.</p>
										<p>The Cedar River is another regulated river. The gaging station near Landsburg, located immediately below the reservoir but above the City of Seattle water diversion, had a weak decreasing trend, while the Cedar River at Renton (near the mouth of the river) showed a strong increasing trend. Summer low flows upstream were almost twice the low flows downstream. The Cedar River is considered important habitat for salmon and suggests that the implementation of the City of Seattle Habitat Conservation Plan is improving summer low flows</p>
										<p>The seven rivers with either strongly or weakly decreasing flows are cause for concern. Some of these water bodies, like Issaquah and Mercer creeks are in urban areas. Others, namely the North Fork Stillaguamish, South Fork Snoqualmie, and Raging rivers, are in areas experiencing population growth. The effect of increased impervious surfaces and ground and surface water withdrawals could be affecting those summer low flow levels. The Deschutes River gaging station is mostly forested land. However, decreasing summer low flows there may be due to forest practices or climate change. </p>
								<p>The Dungeness River showed a weak increasing trend for the upper watershed. Current work to restore flows in the developed areas of the lower watershed is occurring downstream of this gage.</p>
								<p>In general, flows were higher than normal in most Puget Sound rivers and creeks in 2014. Exceptions were the Raging River and Cedar River which had below-average summer low flows. 

								<!--*********************************BLUE HEADER*********************************-->
								<div class="blueBarHeader">What is the indicator? </div>
								<h4 class="blue">Summer low flows</h4>
						
										<p>Low flow occurs during summer months when there is less rain and warmer temperatures. Summer low flow is measured as the minimum 30-day average water flow at river and stream gaging stations maintained by the U.S. Geological Survey from July through October.</p>
										<p>The summer low flow indicator measures trends over a long time period. The indicator tracks how flow conditions are changing over the years, rather than comparing flow levels to a fixed value. The indicator is not sensitive to changes over a shorter time period, which makes it difficult to measure improving trends by 2020, even if significant flow restoration occurs. To measure a change, either large changes in flow must occur, such as a dam setting minimum downstream flows, or a very consistent change over a long period of time will be needed.</p>
										<p>The indicator tests whether the long-term trends of annual summer low flow levels are declining, increasing or stable (no trend). The trend test uses data collected since 1975, representing nearly 40 years of measurements. The advantage of a long-term data set is that the influence of climate changes associated with regional cooling and warming cycles (e.g., the phases of the Pacific Decadal Oscillation) are minimized over time.</p>
										<p>Specific targets were created for 12 rivers in the 2020 Action Agenda. The target language makes a distinction between regulated rivers (i.e. there is one or more dam on the river) and unregulated rivers. Rivers with large dams have highly managed flows and have specified minimum mitigation flows that can have a large influence on the trends in summer low flows. </p>

								<!--*********************************BLUE HEADER*********************************-->
								<div class="blueBarHeader">What are the targets? </div>
								<h5>2020 target</h5>
								<p>By 2020, meet the following river-specific targets:</p>
								<ul>
										<li>Maintain stable or increasing flows in highly regulated rivers: Nisqually, Cedar, Skokomish, Skagit, Green.</li>
										<li>Monitor low flow in the Elwha River after dam removal.</li>
										<li>Maintain stable flows in unregulated rivers that currently are stable: Puyallup, Dungeness, Nooksack.</li>
										<li>Restore low flows to bring the Snohomish River from a weakly decreasing trend to no trend.</li>
										<li>Restore low flows to bring the Deschutes River, North Fork Stillaguamish River, and Issaquah Creek from a strongly decreasing trend to a weakly decreasing trend.</li>
								</ul>
								<h5>Interim targets</h5>
								<p><a href="http://www.psp.wa.gov/downloads/interimtargets/Summer Stream Flows Interim Targets - FINAL.pdf">Link to interim targets (PDF)</a></p>
								<!--*********************************BLUE HEADER*********************************-->
								<div class="blueBarHeader">Is there progress? </div>
								<p>The summer low flow indicator showed some progress relative to the baseline reference. During the period from 1975 to 2013, 83 percent of rivers met their target value for summer low flow trends (10 of 12 rivers). The baseline reference percentage, calculated for the period from 1975 to 2011, was 58 percent (seven of 12 rivers). The Nooksack, Deschutes and Snohomish joined the list of rivers who met their target value. Summer low flow in the Issaquah Creek, North Fork Stillaguamish, Snohomish River as well as Deschutes continue to be concerning, as they have persistently been decreasing since 1975.</p>

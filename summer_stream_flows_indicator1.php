								<h4 class="blue">Why is this happening?			</h4>
								<p>A variety of factors are potential drivers for summer low flow trends. A more detailed analysis is needed to better understand these drivers. However, some observations are possible.</p>
								<p>The principal driver for summer low flows is weather conditions during the previous year. Puget Sound rivers and streams fall into three general categories:</p>
								<ul>
										<li><strong>Rain-dominated</strong>: these basins are at a low elevation where most precipitation is in the form of rain and any snow melts quickly. Summer flows are ground-water dominated.</li>
										<li><strong>Transition, or mixed rain-snow</strong>: these are mid-elevation basins that may have winter snowpack but are also susceptible to rain-on-snow events or lack of snowpack in mild winters. Summer flows come from groundwater, but percolation of snowmelt can have a large effect.</li>
										<li><strong>Snow-dominated</strong>: these basins have significant high elevation areas with deep winter snowpack. Summer flows may be affected by late snowmelt or glacier melt. </li>
								</ul>
								<p>Summer low flows in streams are expected to track the weather conditions across the basin. Therefore, when a basin deviates from the regional tendency for the year, it suggests other drivers are in play. </p>
								<p>Long-term trends in climate change may already be affecting flows in some systems. Glaciers are receding, and the increasing flows in rivers with large glaciers in the headwaters could reflect the rapid loss of glacial mass in the summer. Transition basins are expected to be the most sensitive to changes in snow pack. However, decreased percolation and higher summer temperatures could affect summer flows in rain-dominated basins, too. The specific effect of climate change on Puget Sound rivers and streams needs further analysis.</p>
								<p>Large dams provide the greatest impact on summer low flows. Prior to relicensing of the dams, summer low flows could be greatly reduced from natural conditions due to the operation of the dam to provide hydropower. However, over the last several decades dams have had minimum summer flows set in their licenses. As a result, flows below licensed dams are either stable or increasing, depending on how long the mitigation flows have been in effect.</p>
								<p>Direct diversion of flow from rivers in the summer can be a major driver of low flows. However, for most of the Puget Sound indicator gages there is relatively little agricultural flow diversion. Municipal diversions can have a large effect, but most of these now are subject to licensing requirements or salmon recovery plans. The Cedar River is a good example of this, demonstrated by its strongly increasing flows at the mouth.</p>
								<p>The last major driver is changes in watershed hydrology. Wells can reduce ground water flow that feeds summer low flow, and impervious surfaces can reduce percolation that feeds ground water. Changes in hydrology caused by human activities are more likely to have an impact in urban and rapidly growing residential areas. The fact that many of the rivers and creeks with declining trends are in these areas suggests that this driver may be significant in some areas.</p>
								<p>More detailed analysis is necessary to understand how these many drivers interact in a given basin. A better understanding of the drivers that can respond to changes in watershed and water management could help prioritize basins with declining summer flow trends and direct resources to where they could have the most benefit. Additional resources would need to be identified to do this analysis.</p>
								

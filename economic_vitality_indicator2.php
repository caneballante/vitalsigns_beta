				<h5>Notes about the data:</h5>
                <p>Coastal recreation and tourism is made up of many sectors including boat dealers, restaurants, hotels and lodging, marinas, RV parks and campsites, water tours, sporting goods, recreational services, zoos, and aquaria. Many of these subsectors provide services whose production value cannot be measured as precisely at the local level as a single commodity like timber or fish.</p>
                <p>GDP, a measure of all goods and services associated within an industry, was estimated for coastal tourism at the zip code level by NOAA Office for Coastal Management&rsquo;s Economics: National Ocean Watch program (ENOW) using ratios of state level sector GDP, state level sector wages, and zip code level sector wages. </p>
              <p>Recreation and tourism GDP in Jefferson and Clallam counties accounted for $148 million on average annually from 2005 to 2014. However, those counties are not included in the figures above because their recreational activity cannot be accurately divided between the Puget Sound and Pacific coast.  Additionally, GDP in Thurston County is not estimated by ENOW because the portion of coastal tourism and recreation that can be attributed to the Puget Sound versus the professional and political activity at the state&rsquo;s capital is unclear. For these reasons, recreation and tourism estimates above should be considered conservative.</p>
				<h5>Why do we think this is happening?</h5>
              <ul>
                <li>Recent declines in shellfish aquaculture production value can be mainly attributed to the 2013 ban from China on Puget Sound geoduck imports. </li>
                <li>The downward swing in timber production value may not only be due to the great recession and decline in housing starts but to the amount of land in timber production, growth and mortality, management costs including taxes and regulatory compliance, perceived risks due to markets and regulation, strength of domestic and international markets for forest products, and varying management objectives of very diverse types of timber ownership. These factors may also be in part what are driving increases in production value since 2010.</li>
                <li>Decline in employment in commercial fishing also confirms the decrease in production values since 2011. There is some suggestion that the decline in production values is due in part to drought and low runs during those years. </li>
                <li>Increases in tourism and recreational GDP may be a result of increased consumer demand for farm to table destination dining, cruises ship and tribal casinos, increased outdoor recreation access (kayaking, stand up paddle boarding, and etc.), , and development of tourism infrastructure such as hotels and VRBO accommodations. Some part of this increased demand suggest that there is a consumer willingness to pay for the nonmarket attributes of the Puget Sound ecosystem such as recreation, aesthetics, and culture. </li>
              </ul>

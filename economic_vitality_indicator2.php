			<h5>Notes about the methods and the data: </h5>
			<p>The annual value of timber is derived from the Washington State Department of Revenue (WADOR) forest excise tax distributions. The 5% WADOR tax is assessed on the stumpage value at the time of harvest, and captures activity on state, federal, and private lands. The value of agricultural products is reported by the U.S. Department of Agriculture (USDA) census of agriculture every five years. FIGURE 2 provides a trend between 1992, 2007 and 2012. <strong>Agriculture includes crop and livestock sales.</strong> </p>
			<p>The geographical scope of the Puget Sound Partnership includes all counties adjoining the Puget Sound basin. The outer coast of the state is not included. To restrict annual timber and agricultural value estimates appropriately, WADNR forest practice and WSDA agricultural land use maps were intersected with county and Puget Sound Partnership boundaries. The percentage of timber or agricultural land use that fell within the Puget Sound Area was used to draw a proportion of total county sales. </p>
			<p>The value of fishing and aquaculture products is provided through Pacific Fisheries Information Network or PacFIN. PacFIN data reported here is the &ldquo;ex-vessel&rdquo; value received by fisherman for fish and shellfish caught in the Puget Sound catch areas (Figure 4), and landed in Puget Sound counties. Because of sensitivity in data reporting, fish and shellfish value includes both tribal and non-tribal fishing activity.</p>
			<p><strong>Aquaculture value consists of farmed shellfish, while fishing value includes wild caught shellfish including crab and shrimp, as well as finfish</strong></p>

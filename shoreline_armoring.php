										<li>Consider using soft shore techniques such as large wood or gravel berms to provide wave protection, planting native vegetation and installing adequate drainage to stabilize slopes. You can get ideas in the<a href="http://wdfw.wa.gov/publications/01583/wdfw01583.pdf" target="new"> Marine Shoreline Design Guide</a></li>

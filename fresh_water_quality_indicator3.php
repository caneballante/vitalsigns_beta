				<p>Not surprisingly, Benthic Index of Biotic Integrity (B-IBI) scores were lower in areas with greater urban development (Figure 8). The B-IBI is highly correlated with development and component metrics respond to specific aspects of disturbance. For example, long-lived species tend to decline as stream flows become higher in wet periods and lower in dry periods. Stoneflies also decline when natural vegetation near the stream is removed. Stream invertebrates are also sensitive to sediment, toxics, increased temperatures, and loss of habitat.</p>

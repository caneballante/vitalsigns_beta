            <h5>Is the indicator making progress toward the 2020 target? </h5>
            <p><span><img src="images/status_mixed_results.png" width="118" height="121" alt=""/>The results for this indicator are mixed.  </span></p>
            <p>Progress toward these 2020 freshwater quality targets was mixed, at best. Overall, biological condition at some sites is declining, improving at others, and not changing at most. Because the B-IBI results indicate degradation in some streams ranked as excellent, but yet a net improvement in the biological condition of streams previously ranked fair by the B-IBI, there is mixed progress toward the 2020 target.</p>
<ul><li>Only 76 percent of streams previously ranked as excellent maintained their excellent ranking (Part 1 of the target).</li>
<li>The B-IBI results also indicate that more fair stream sites improved to good or excellent than declined to poor or very poor during 2011-2014 compared to 2006–2009 (Part 2 of the target).</li></ul>

<p>From 2007-2011, a total of 245 stream sites were sampled more than once. Of these, a total of 128 sites had initial B-IBI scores indicating “fair” condition (Figure 4 in Latest data and maps sections). The biological condition of most of these sites did not change. Consistent with the 2020 target, 11 of the 128 sites improved and changed categories to “good” or “excellent". However, 26 stream sites declined and changed status from “fair” to “poor” or “very poor”. The net difference in the change in status of 15 streams represents an overall decline (12 percent) in the biological condition of "fair" streams.</p>
<p>For the streams with “excellent” biological condition as rated by the B-IBI, some streams are already protected. A detailed analysis has not been done to identify which streams and watersheds should be protected for this target. The watersheds will likely be small, five to 20 square miles.</p>

            
			

<p>
  The Benthic Index of Biotic Integrity (B-IBI) describes the biological condition of stream sites and their surrounding habitat based on the diversity and relative abundance of the benthic (bottom dwelling) macroinvertebrates found at the site. These macroinvertebrates include aquatic insect larvae, including mayflies, stoneflies, caddisflies, beetles, flies, dragonflies, as well as other invertebrates such as worms, snails, and many others. Ten measures of biological condition are scored and summarized as the B-IBI, which ranges from a score of 0, indicating a very poor stream condition, to 100, indicating excellent condition. </p>
  <p>Benthic Index of Biotic Integrity data are routinely collected and reported by more than 20 local jurisdictions, tribes, and other state and federal organizations in Puget Sound for a variety of reasons. The Washington Department of Ecology samples 50 randomly selected stream and river sites every four years to get an unbiased estimate of regional conditions. Snohomish and King Counties also randomly select stream sites to estimate regional condition using the B-IBI. </p>
  <p>Change over time is calculated as the difference in average scores between two time periods, 2006-2009 and 2013-2016. There were 354 sites across Puget Sound that were sampled at least once during both of these time periods.</p>

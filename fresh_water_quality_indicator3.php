<p>For sites with repeat visits during the last five years, more sites have declined in biological condition from &ldquo;fair&rdquo; to &ldquo;poor&rdquo; or &ldquo;very poor&rdquo; (29 percent) than have improved to &ldquo;good&rdquo; or &ldquo;excellent&rdquo; condition (9 percent; Figure 3). These B-IBI scores were not derived from a random sample design and, therefore, do not necessarily represent the entire Puget Sound area.</p>
<p>For 84 sites with long-term data in King County, scores for 68 sites did not change (81 percent), 10 improved (12 percent), and six declined (7 percent).</p>

				<p> Benthic Index of Biotic Integrity.</p>
				<h4 class="aqua">Benthic Index of Biotic Integrity</h4>
				<p>The Benthic Index of Biotic Integrity (B-IBI) describes the biological condition of stream sites and their surrounding habitat based on the diversity and relative abundance of the benthic (bottom dwelling) macroinvertebrates living there, such as mayfly larvae, stonefly larvae, caddisfly larvae, worms, beetles, snails, dragonfly larvae, and many others.</p>
				<p>Ten measures of biological condition are scored and summarized as the B-IBI, which ranges from a score of 10, indicating a very poor stream condition, to 50, indicating excellent condition.</p>
				<p>Benthic Index of Biotic Integrity data are routinely collected and reported by more than 20 local jurisdictions, tribes, and other state and federal organizations in Puget Sound for a variety of reasons. The Washington Department of Ecology samples  50 randomly selected stream and river sites every four years to get an unbiased estimate of regional conditions. Snohomish and King counties also randomly select stream sites to estimate regional condition using the B-IBI. </p>
				
				<!--*********************************AQUA HEADER*********************************-->
				<div class="aquaBarHeader">What is the target? </div>
				<h5>2020 target</h5>
				<ul>
					<li>Protect all small streams that are currently ranked “excellent” by the Benthic Index of Biotic Integrity for biological condition; and improve and restore at least 30 streams ranked “fair” so their average scores become “good.”</li>
				</ul>
				<h5>Interim targets</h5>
				<p>Interim targets have not been set for these indicators</p>
				
				<!--*********************************AQUA HEADER*********************************-->
				<div class="aquaBarHeader">Is there progress? </div>
				<p>Progress toward the 2020 freshwater quality targets was mixed, at best. The total number of impaired river and stream segments was down in 2008-2010, which represents some improvement, but the trend is expected to reverse in the next round of assessments. However, freshwater quality, as measured by the Water Quality Index for streams and rivers, was slightly worse than the baseline reference. Finally, there was a net decline in the biological condition of small streams as shown by the Benthic Index of Biotic Integrity.</p>
				<p>&nbsp;</p>
				<h4 class="aqua">Benthic Index of Biotic Integrity</h4>
				<p>No progress has been made in improving the biological condition of small streams. Overall, the biological condition of streams initially ranked as "fair" has declined, based on the Benthic Index of Biotic Integrity scores.</p>
				<p>From 2007-2011, a total of 245 stream sites were sampled more than once. Of these, a total of 128 sites had initial B-IBI scores indicating “fair” condition (Figure 4 in Progress summary and data sections). The biological condition of most of these sites did not change.  Consistent with the 2020 target, 11 of the 128 sites improved and changed categories to “good” or “excellent". However, 26 stream sites declined and changed status from “fair” to “poor” or “very poor”. The net difference in the change in status of 15 streams represents an overall decline (12%) in the biological condition of "fair" streams.</p>
				<p>For the streams with “excellent” biological condition as rated by the B-IBI, some streams are already protected. A detailed analysis has not been done to identify which streams and watersheds should be protected for this target. The watersheds will likely be small, five to 20 square miles.</p>

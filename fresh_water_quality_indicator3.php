<p>From 2007 to 2011, B-IBI was measured more than once at 245 sites. Of these, 128 stream sites were raided as &ldquo;fair&rdquo; by B-IBI for the first visit. Of these, 11 improved in condition to &ldquo;good&rdquo; or &ldquo;excellent: condition; 26 declined in condition to &ldquo;poor&rdquo; or &ldquo;very poor,&rdquo; and 91 were still rated as &ldquo;fair.&rdquo;<br />

				<p>Puget Sound supports about 55,000 acres of eelgrass (average 2009-2015). Sound-wide eelgrass area has not increased relative to the 2000-2008 baseline. There is no significant trend in eelgrass area between 2009 and 2015.</p>
				<p>Eelgrass distribution patterns vary across Puget Sound, with two main types of eelgrass beds: narrow fringing beds and broad beds on shallow flats. Over 20% percent of the total eelgrass area occurs in only two embayments: Padilla and Samish Bay.</p>

 <h3>FIGURE 2.</h3> 
                <h5>COUNT OF EELGRASS GAINS AND LOSSES ON A SITE-BY-SITE BASIS <br />
                <em>2000 – 2015 (medium-term) and 2010-2015 (recent)</em></h5>
				<h4 class="purp"><img src="images/eelgrass_2015_chart1-02.png" width="900" height="450" alt="Status of eelgrass sites in Puget Sound" /></h4>
				<blockquote><p>Number of sites with gains or losses in eelgrass area. The “other” category includes sites with no detectable change or no seagrass bed present.<br />
					<em>Source: Washington Department of Natural Resources, Submerged Vegetation Monitoring Program</em></p></blockquote>

				
				<p>When looking at change on a site by site basis, patterns vary. Between 2000 and 2015, a total of 514 sites were sampled as part of the monitoring effort led by the Washington State Department of Natural Resources. From these sites, 358 were sampled over multiple years and classified for eelgrass trends over time. Most of these sites were stable throughout the entire monitoring record. Twenty-eight sites decreased in eelgrass area, 30 sites increased in eelgrass area, 247 sites experienced no detectable change, and 53 sites did not have seagrass present.</p>
				  <p>In recent years, eelgrass conditions have improved. Out of the 177 sites sampled more than once between 2010 and 2015, there were more sites with increasing (18) than decreasing (4) eelgrass area. Recent increases in site eelgrass area do not always compensate for the losses seen over the last decade, but nonetheless reflect a recent reversal in trend.</p>
			   <p>At the sub-basin scale, lower Hood Canal, southern Puget Sound, and the San Juan Islands have the greatest number of sites where the amount of eelgrass decreased, including several sites where eelgrass beds completely disappeared (see maps). </p>
				<p>Restoration actions may be leading to increases in eelgrass area at river deltas, an important habitat for salmon and many other organisms. At the Skokomish River in lower Hood Canal, notable increases in eelgrass area were observed following removal of dikes and other restoration activities. </p>
				<h5>Long-term trends in select areas in Puget Sound</h5>
				<p>A recent analysis of data from WDFW herring spawn surveys (Shelton et al. 2016) found that eelgrass in Puget Sound has not experienced major declines at herring spawning areas over the past 40 years. This is good news, and it sets Puget Sound apart from many other developed areas, where catastrophic declines have occurred. </p>
				<p>Out of the 14 herring spawn areas surveyed, 3 areas showed increases, 5 areas showed declines, and 6 areas were stable between 1970 and 2012. At local scales, several sites with long-time declines were detected. This pattern of local declines agrees with medium-term monitoring results from The Washington State Department of Natural Resources. Heads of bays seem particularly vulnerable: significant long-term declines have been detected in Quartermaster Harbor, Port Gamble Bay, Westcott Bay and Garrison Bay.</p>
				<p>Data on eelgrass abundance in Puget Sound pre-1970 are limited, but there is evidence for declines at several locations throughout the Sound <em>(Thom and Hallum, 1990)</em>. The extensive development of Puget Sound pre-1970 is thought to have led to widespread loss of eelgrass in Puget Sound <em>(Shelton et al. 2016)</em>.</p>
				<h5>Future prospects for eelgrass in Puget Sound</h5>
				<p>Over the last 40 years, a wide range of environmental legislation has improved and protected water quality and nearshore ecosystems in Puget Sound. Examples are the Clean water Act (1972), which gave the US Environmental Protection Agency the authority to regulate waste water discharge in waters of the United States, the Washington State Shoreline Management Act (1972), which is aimed at protecting the shoreline and its natural resources, and the designation of eelgrass as a habitat of special concern by the Washington State Department of Fish and Wildlife. The fact that eelgrass beds in the herring spawn areas of Puget Sound have endured since the 1970’s, despite the large increases in population, suggests that these efforts have paid off. However, the long-term declines in approximately one third of the herring spawn areas, and in many localized areas throughout the Sound, are a reason for concern. </p>
				<p>Eelgrass shows resilience to relatively wide ranges in natural conditions such as El Niños. However, continued eelgrass losses in subareas of Puget Sound could eventually result in local collapse of eelgrass populations, because the sources of renewal (i.e., seeds) of eelgrass on the broad scale could be compromised.  Continued management efforts will be critical to avoid catastrophic declines and to restore the areas with documented losses in Puget Sound. Ongoing efforts like the NOAA and DNR studies are improving our scientific understanding of eelgrass at these locations. </p>
				<p>In order to ensure the protection of eelgrass, the Puget Sound Partnership set a management goal of a 20% in eelgrass area by 2020, which was chosen based on gains achieved in other regions in response to protection and restoration actions. The Puget Sound Partnership and the Washington State Department of Natural Resources developed an Eelgrass Recovery Target Strategy in 2015, to identify pathways towards reaching the management goal by 2020. At this point in time, it is difficult to predict whether the goal of 20% increase by 2020 will be met. The stresses on eelgrass in Puget Sound will likely need to be reduced to see Sound-wide gains in eelgrass area and health.</p>

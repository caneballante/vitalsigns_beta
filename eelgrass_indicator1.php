               <h3>FIGURE 1.</h3> 
                <h5>ACRES OF EELGRASS IN PUGET SOUND <br />
  <em>2000 – 2008 baseline average and 2009 – 2015 annual estimates</em></h5>
				<img src="images/eelgrass_2015_chart1-01.png" width="600" height="382" alt="Acres of eelgrass in Puget Sound, in thousands, 2000-2008 baseline and 2009-2015 annual data" />
			  <p>Puget Sound supports about 54,000 acres of eelgrass beds (the 2000 to 2014 average).  Sound-wide eelgrass area has not increased relative to the 2000-2008 baseline. There is no significant trend in eelgrass area between 2009 and 2014.</p>
<p>Eelgrass distribution patterns vary across Puget Sound, with two main types of eelgrass beds: narrow fringing beds and broad beds on shallow flats. Approximately 24 percent of the total eelgrass area occurs in only two embayments: Padilla and Samish Bay.</p>

				<h4 class="purp"><img src="images/eelgrass_2015_chart1-02.png" width="550" height="586" alt="Status of eelgrass sites in Puget Sound" /></h4>
				<p>When looking at change on a site by site basis, patterns vary. Between 2003 and 2014, a total of 392 sites have been sampled as part of the Submerged Vegetation Monitoring Program lead by the Washington State Department of Natural Resources. From these sites, 313 were sampled over multiple years and classified for eelgrass trends over time. Most of these sites were stable throughout the entire monitoring record. Twenty-five sites decreased in eelgrass area, 22 sites increased in eelgrass area, 225 sites experienced no detectable change, and 41 sites did not have seagrass beds present.</p>
				  <p>In recent years, eelgrass conditions have improved. Out of the 138 sites sampled between 2010 and 2014 and sampled more than once, there were more sites with increasing (17) than decreasing (2) eelgrass area. The recent increases in eelgrass area at these sites do not compensate for the local losses seen over the last decade, but nonetheless reflect a recent reversal in trend.</p>
				  <p>At the sub-basin scale, lower Hood Canal, southern Puget Sound, and the San Juan Islands have the greatest number of sites where the amount of eelgrass decreased, including several sites where eelgrass beds completely disappeared (see maps).</p>
				  <p>Restoration actions may be leading to increases in eelgrass area at river deltas, an important habitat for salmon and many other organisms. At the Skokomish River in lower Hood Canal and the Nisqually River in southern Puget Sound, notable increases in eelgrass area were observed following removal of dikes and other restoration activities.</p>
			    <p>Despite these efforts, it is difficult to predict whether the goal of 20% increase by 2020 will be met. The stresses on eelgrass in Puget Sound will likely need to be reduced to see Sound-wide gains in eelgrass area and health.</p>
<h4 class="purp">Hood Canal</h4>
				<p>In many places throughout the world, eelgrass is threatened by excess nitrogen loading from human sources. These extra nutrients contribute to the excessive growth of macroalgae in eelgrass beds, and promote the growth of plankton, which reduces water clarity. This causes stress to eelgrass and contributes to its observed decline.</p>
			    <p>Eelgrass in lower Hood Canal has been showing signs of long-term decline, which may be related to nutrient stress. While the majority of nitrogen loading in this area is not from human sources, water quality in lower Hood Canal can be a concern because of stratification and long residence times. Recently, eelgrass in lower Hood Canal has increased. Gains at the Skokomish River delta could be related to delta restoration activities. Increases at other sites could be related to natural conditions or to management actions.</p>
<h4 class="purp">Effects of delta restoration</h4>
				<p>Along the Skokomish River delta, three sites have gained more than 200 acres of eelgrass. The eelgrass increases were first noted in 2010, following restoration work that was initiated in 2006 to remove dikes and restore tidal channels. Subsequent increases in eelgrass area were observed in 2014. At the Nisqually River delta, eelgrass area increased by at least 75 acres between 2006 and 2014. These increases followed the largest dike removal effort in the Pacific Northwest in 2009 (<a href="http://nisquallydeltarestoration.org" target="_blank">http://nisquallydeltarestoration.org</a>).</p>
<h4 class="purp">The San Juan Islands</h4>
				<p>Eelgrass can grow to more than 10 meters deep in the clear water surrounding the San Juan Islands. Despite the generally high water quality, some eelgrass beds in shallow embayments of the San Juan Islands show signs of decline. While the causes of these declines are not yet known, researchers at Friday Harbor Laboratories have hypothesized that increased sediment sulfide concentrations impact the survival of young plants at these locations. Another potential cause is the occurrence of infections with Labyrinthula, a microorganism that causes eelgrass wasting disease.</p>

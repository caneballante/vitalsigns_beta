								<h4 class="purp">Soundwide eelgrass area</h4>
						
<p>Eelgrass is an important submerged marine plant growing throughout Puget Sound. Changes in the
abundance or distribution of this resource reflect changes in environmental conditions.</p>

<p>Eelgrass and other seagrass species are used as indicators of ecosystem health throughout the world because they respond to many natural and human-caused environmental factors that affect water quality. These factors are also likely to affect many other species that depend on eelgrass habitat.</p>

<p>For example, excess nutrients, sewage, and algae can reduce water clarity, while storms, runoff, and dredging can stir up sediment, preventing light from penetrating the water and reaching the eelgrass. Boat wakes, propellers, and docks can also disturb eelgrass beds.</p>

<p>Since eelgrass is protected by many regulations, its condition reflects, in part, the success of management actions. The Washington State Department of Natural Resources assesses status and trends in eelgrass by evaluating eelgrass area and depth range at more than 100 sites throughout Puget Sound annually.
</p>
<p>Two measures are used to demonstrate eelgrass status and trends in Puget Sound:</p>
<ol>
<li> Soundwide native seagrass area: the total area of eelgrass and other native seagrass species in Puget Sound. </li><li>Number of increasing, decreasing, or stable eelgrass beds: count of eelgrass gains and losses on a per site basis.</li>
</ol>
							
								<!--*********************************PURPLE HEADER*********************************-->
								<div class="purpBarHeader">What are the targets?</div>
								<h5>2020 target</h5>
								<p>A 20% increase in the area of eelgrass in Puget Sound relative to the
2000-2008 baseline reference by the year 2020.</p>
									<h5>Interim targets</h5>
								<p><a href="http://www.psp.wa.gov/downloads/interimtargets/Eelgrass Interim Targets - FINAL.pdf">Link to interim targets (PDF)</a></p>
								<!--*********************************PURPLE HEADER*********************************-->
								<div class="purpBarHeader">Is there progress? </div>
                                 <p>Soundwide eelgrass area has changed little since the monitoring program started. Between 2000 and 2008, total eelgrass area in Puget Sound was approximately 53,300 acres. Currently, the soundwide estimate of eelgrass cover hovers around 57,000 acres, based on the 2011 to 2013 average. Though the 2011-2013 acreage represents approximately a 7% increase relative to the 2000-2008 baseline, the increase is small and has a wide uncertainty range. There has been little progress towards the 2020 target of a 20% increase in soundwide area, but recent increases in eelgrass area at local scales provide reason for cautious optimism regarding future gains.</p>
							
										<p>Between 2000 and 2013, the Washington State Department of Natural Resources sampled eelgrass at
								347 sites across Puget Sound. The majority of these sites were stable: they showed no changes in eelgrass area since monitoring began (209 sites). Between 2003 and 2013, there were slightly more sites with decreasing trends (25) in eelgrass area than sites with increases in eelgrass area over time (17). In recent years there has been a shift in this pattern. From 2010 to 2013, several sites that were previously declining have stabilized or show a recent increase in eelgrass area. It is too early to tell if this trend will continue over the coming years, or if it is a temporary deviation from the long-term pattern.</p>


<p>Restoration actions may be leading to increases in eelgrass area at river deltas, an important habitat for salmon and many other organisms. At the Skokomish River in lower Hood Canal and the Nisqually River in southern Puget Sound, notable increases in eelgrass area were observed following removal of dikes and other restoration activities. Despite these efforts, it is difficult to predict whether the goal of 20% increase by 2020 will be met. The stresses on eelgrass in Puget Sound will likely need to be reduced to see soundwide gains in eelgrass area and health.</p>
							
							

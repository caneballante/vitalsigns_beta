								<h4 class="purp">&nbsp;</h4>
								<img src="images/eelgrass_2015_chart1.png" width="550" height="586" alt="Acres of eelgrass in Puget Sound, in thousands, 2000-2008 baseline and 2009-2011 annual data" />
<p>Soundwide eelgrass area has not increased relative to the 2000-2008 baseline. There is no significant trend in eelgrass area between 2009 and 2013.</p>


<p>Puget Sound supports roughly 55,000 acres of eelgrass beds (long term average 2000-2013). Eelgrass distribution patterns vary across Puget Sound, with two main types of eelgrass beds: narrow fringing beds and broad beds on shallow flats. Approximately 25% of the total eelgrass area occurs in only two embayments: Padilla and Samish Bay.</p>


<p>The total amount of eelgrass in Puget Sound has been relatively stable from 2000 to 2013. When looking on a site by site basis, the situation is more dynamic. At the majority of the sampled sites, there was no change or trend in the size of eelgrass beds. Lower Hood Canal and southern Puget Sound have the greatest number of sites where the amount of eelgrass decreased, including several sites where eelgrass beds completely disappeared (see maps).
</p>
			

<h4 class="purp"><img src="images/eelgrass_2015_chart2.png" width="550" height="586" alt="Status of eelgrass sites in Puget Sound" /></h4>
<p>When comparing long-term (2003-2013) with recent (2010-2013) trends, there is a higher percentage of sites with gains in eelgrass area in the recent years (16% vs 5%).</p>

<p>Between 2003 and 2013, a total of 347 sites were classified for eelgrass trends over time.  Most of these sites were stable throughout the entire monitoring record. Twenty-five sites decreased in eelgrass area, 17 sites increased in eelgrass area, 209 sites experienced no detectable change, and 60 sites did not have seagrass beds present. Thirty-six sites had insufficient data for trend analysis (sampled only 1 year). In recent years, eelgrass conditions have improved. Out of the 156 sites sampled between 2010 and 2013, there were more sites with increasing (25) than decreasing (5) eelgrass area.  The recent increases in eelgrass area at these sites do not compensate for the local losses seen over the last decade, but nonetheless reflect a recent reversal in trend.</p>


<h4 class="purp">Hood Canal</h4>
<p> In many places throughout the world, eelgrass is threatened by excess nitrogen loading from human
sources. These extra nutrients contribute to the excessive growth of macroalgae in eelgrass beds, and promote the growth of plankton which reduces water clarity. This causes stress to eelgrass and contributes to its observed decline. Eelgrass in lower Hood Canal has been showing signs of long term decline, which may be related to nutrient stress. While the majority of nitrogen loading in this area is not from human sources, water quality in lower Hood Canal can be a concern because of stratification and long residence times.</p>



<p>Recently, eelgrass at some sites in Hood Canal has increased. Gains at the Skokomish River delta could be related to delta restoration activities. Increases at other sites could be related to natural conditions or to management actions.</p>
<h4 class="purp">Effects of delta restoration</h4>
<p>Along the Skokomish River delta, three sites have gained more than 200 acres of eelgrass. The eelgrass increases were first noted in 2010, following restoration work that was initiated in 2006 to remove dikes and restore tidal channels. Subsequent increases in eelgrass area were observed in 2013. At the Nisqually River delta, eelgrass area increased by at least 75 acres between 2006 and 2013. These increases followed the largest dike removal effort in the Pacific Northwest in 2009 (http://nisquallydeltarestoration.org).</p>
<h4 class="purp">The San Juan Islands</h4>
<p>Eelgrass can grow to more than 10 meters deep in the clear water surrounding the San Juan Islands. Despite the generally high water quality, some eelgrass beds in shallow embayments of the San Juan Islands show signs of decline.  While the causes of these declines are not yet known, researchers at Friday Harbor Laboratories have hypothesized that increased sediment sulfide concentrations impact the survival of young plants at these locations. Another potential cause is the occurrence of infections with Labyrinthula, a microorganism that causes eelgrass wasting disease.</p>

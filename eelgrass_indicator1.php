				<p>This indicator is the acres of eelgrass throughout the sound.</p>
				<p>Eelgrass is an important submerged marine plant growing throughout Puget Sound. Changes in the abundance or distribution of this resource reflect changes in environmental conditions.</p>
				<p>Eelgrass and other seagrass species are used as indicators of ecosystem health throughout the world because they respond to many natural and human-caused environmental factors that affect water quality. These factors are also likely to affect many other species that depend on eelgrass habitat.</p>
				<p>For example, excess nutrients, sewage, and algae can reduce water clarity, while storms, runoff, and dredging can stir up sediment, preventing light from penetrating the water and reaching the eelgrass. Boat wakes, propellers, and docks can also disturb eelgrass beds.</p>
				<p>Since eelgrass is protected by many regulations, its condition reflects, in part, the success of management actions. The Washington State Department of Natural Resources assesses status and trends in eelgrass by evaluating eelgrass area and depth range throughout Puget Sound.</p>
				<p>Two measures are used to demonstrate eelgrass status and trends in Puget Sound:</p> 
				
				<ol><li>Soundwide native seagrass area: the total area of eelgrass and other native seagrass species in Puget Sound.</li>
				  <li>Number of increasing, decreasing, or stable eelgrass beds: count of eelgrass gains and losses on a per site basis.</li></ol>

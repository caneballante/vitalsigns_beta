				<p>Commercial fishing is a key industry in Puget Sound. Millions of dollars of revenue are generated annually from fish sales. The 17 federally recognized tribes in Puget Sound, along with the State of Washington, jointly manage the fish and shellfish resources. By treaty, tribal fishers collectively and non-tribal fishers collectively are each entitled to up to one-half of the harvestable amount. Every year, limits are set based upon a complicated set of factors that are used to predict how many fish will be available for harvest, taking into account the status of protected and non-protected stocks. Overall harvest limits are set to ensure that harvests are sustainable and there will be adequate salmon resources into the future. Then this must be divided into commercial, recreational, subsistence, and ceremonial harvest. Our long-term vision, as recovery proceeds, is to restore the ecosystem and health of Puget Sound to ensure sustainable ongoing fishing, including commercial.</p>

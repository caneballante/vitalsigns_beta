			<h3><a href="#!">Importance to Puget Sound recovery</a></h3>
			<div>
				<p>Much of the “floor” of Puget Sound is covered with sediment—the gravel, sand, silt, and clay that has accumulated over years, decades, centuries, and even millennia. The accumulation of sediment is a natural estuarine process that occurs as beaches and bluffs erode, as streams and rivers carve their way through watersheds and carry sediments from the land into the water, as glaciers grind down the rocks of mountains, and even as the teeming algae and microscopic animals die and settle slowly to the bottom. </p>
				<p>These sediments form a unique habitat that is home to clams, marine worms, burrowing shrimp, bottom-dwelling fish, and thousands of other unique species that live in, or on, the bottom sediments. In turn, these animals form a critical part of the marine food web, help filter the overlying water, and even process and help breakdown the sediments themselves—much as earthworms and other soil organisms process and enrich the soils of our farms, gardens, and forests.</p>
				<p>In a well-functioning estuary, marine sediments support a healthy biological community. But in Puget Sound sediments have become contaminated and adversely affect aquatic life that rely upon them.</p>
			</div>
			

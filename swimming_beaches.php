						<h3><a href="#">Importance to Puget Sound Recovery</a></h3>
						<div>
								<p>On a warm day, the waters of Puget Sound present an alluring invitation to wade, swim, or SCUBA dive. Although many of our beaches meet high standards for water quality, every year beaches are closed to the public because of high bacteria counts. </p>
								<p>In 2011, one quarter of our monitored beaches were unsafe for swimming because they failed to meet water quality standards. Swimming in contaminated waters can result in a variety of illnesses and other unpleasant outcomes. </p>
									<p>	As our region grows in population, we can expect both an increase in the demand for recreational swimming opportunities, and in the sources of contamination from wastewater and stormwater runoff.</p>
								<p>Clean water, free of harmful bacteria or chemicals, is an important goal in our efforts to restore and protect Puget Sound. We want the water to be as clean as possible so that we can enjoy the Sound without worrying about our health. </p>
						
								

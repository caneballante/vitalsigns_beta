				<p>This indicator is onsite sewage inventory, inspection, and repair.</p>
				  <p>This indicator tracks and advances the proper use and care of onsite sewage systems (septic systems) in sensitive and high-risk areas of Puget Sound, in order to protect public health and water quality. The indicator consists in three distinct measures: inventory, inspection and repair of onsite sewage systems in designated areas. The designated areas include Marine Recovery Areas and other areas with comparable requirements. State rules require all homeowners to regularly inspect and maintain their onsite sewage systems. However, in Marine Recovery Areas and other designated areas, local health jurisdictions engage more directly with homeowners to help ensure systems are inspected and maintained to reduce public health risks.</p>
				  <p>The 12 Puget Sound local health jurisdictions (LHJs) report data semi-annually to the Washington State Department of Health (DOH). Data for the 2020 target were compiled starting in 2011 and four reporting cycles have now been completed.</p>
			   <p>All 12 Puget Sound local health jurisdictions have adopted comprehensive management plans for onsite sewage systems under the state onsite sewage rule. The management plans frame the local Operation &amp; Maintenance programs. While the 12 local Operations &amp; Maintenance programs have common elements, they are all uniquely designed and implemented. The Department of Health oversees the statewide onsite sewage rule and collects and interprets local data for the Puget Sound targets.</p>
				

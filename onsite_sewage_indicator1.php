		<p>Progress toward the 2020 target to inventory, inspect and repair onsite sewage systems in designated areas is mixed. There have been advances in inventorying and inspecting septic systems. However, the analysis of progress on fixing failures of septic systems is pending due to lack of data.</p>
		  <p>The 12 Puget Sound local health jurisdictions (LHJs) have inventoried nearly 67,000 onsite sewage systems in Marine Recovery Areas and other specially designated areas since 2011. The inventory includes septic systems with official records and that are current with inspection, systems that are overdue for an inspection, and systems that are suspected to exist but are not officially documented. Since 2011, the baseline reference year, nearly 14,000 additional septic systems were added to the inventory. The overall percentage of onsite sewage systems that have been inventoried and documented rose from 86 percent to 95 percent during this period and reflects progress toward the 2020 target of 100 percent.</p>
		  <p>The percent of septic systems that have been inspected and the inspection is current increased from 33 percent to 41 percent between 2011 and 2016. Although there has been progress relative to the baseline reference, the indicator is still far from reaching the 2020 target value of 95 percent. Working with homeowners to ensure and track ongoing regular inspections is challenging and costly. State and federal funds have provided additional resources for work on the 2020 targets.</p>
		  <p>The third measure of this target is to fix all failures in the designated areas. The analysis is pending due to lack of data tailored to the target. The Washington State Department of Health (DOH) is working with the LHJs to standardize an approach to track and report system repairs.</p>
				<h5>Why is it happening?</h5>
		  <p>The local health jurisdictions are currently working to adapt and align their programs to fit with these ambitious regional targets.</p>
		  <p>The Puget Sound Operations &amp; Maintenance (O&amp;M) programs in local health jurisdictions are inherently complex and costly to implement. They all work from the same rule requirements and core elements, but are all tailored to local conditions, budgets, and ways of doing business. They require significant planning, infrastructure, personnel, public education, political support, community buy-in, financial resources, and smart execution.</p>
		  <p>At all levels of government, funding for decentralized wastewater programs and infrastructure dramatically lags behind public investment in centralized sewer systems. State financial support for the Puget Sound O&amp;M programs has never materialized at a scale originally envisioned when the state onsite sewage and Marine Recovery Area laws were enacted. Most O&amp;M program costs are covered locally and are complemented by state and federal grants. The Washington State Department of Health (DOH) surveyed the local programs in 2014 to determine financial need. The analysis documented highly variable local expenditures ($6+ million including grants) and needs, and estimated total annual need at more than $14 million to implement current programs, and more than $24 million to carry out baseline services regionally.</p>
		  <p>The targets provide a small window into the workings of the local O&amp;M programs. These programs include such diverse activities as financial lending for system repairs, code enforcement, homeowner inspection training, data management, certification of O&amp;M professionals, homeowner notification and reporting, and community outreach.</p>
		  <p>Management—characterized here as O&amp;M—has long been recognized as the weak link in the widespread use of onsite sewages systems when compared to centralized sewers. The picture is gradually changing in the Puget Sound region as local O&amp;M programs take root, but it will continue to take significant investments and smart thinking to effectively design and deliver these utility-style programs and services on an ever-expanding scale.</p>
		  <p>Homeowners and elected officials alike are increasingly seeing the need for and benefits of these programs. The Action Agenda and regional targets will continue to shape and guide these efforts.</p>
				<h5>Target 2: Program extension to unsewered shoreline.</h5>
		  <p>There has been no progress toward the target of expanding Marine Recovery Areas and sensitive area designations to 90 percent of Puget Sound's unsewered shoreline. The Department of Health estimates that the existing designated areas cover approximately 450 miles of unsewered Puget Sound shorelines. This represents about 20 percent of Puget Sound&rsquo;s unsewered shorelines, only a fraction of the target for 2020 (90 percent).</p>
		  <p>The law can only be applied in areas with documented water quality impacts associated with existing onsite septics, and there may be additional legal and administrative issues limiting the expansion of MRAs to meet this target.</p>

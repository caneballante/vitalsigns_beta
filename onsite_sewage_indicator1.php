								<div class="greenBarHeader">What is the indicator? </div>
								<p>On-site sewage inventory, inspection, and repair</p>
								<p>This indicator tracks and advances the proper use and care of on-site sewage systems (septic systems) in sensitive and high-risk areas of Puget Sound, in order to protect public health and water quality. The indicator consists in three distinct measures: inventory, inspection and repair of on-site sewage systems in designated areas. The designated areas include Marine Recovery Areas and other areas with comparable requirements. State rules require all homeowners to regularly inspect and maintain their on-site sewage systems. However, in Marine Recovery Areas and other designated areas, local health jurisdictions engage more directly with homeowners to help ensure systems are inspected and maintained to reduce public health risks.</p>
								<p>The 12 Puget Sound local health jurisdictions (LHJs) report data semi-annually to the Washington State Department of Health (DOH). Data for the 2020 target were compiled starting in 2011 and four reporting cycles have now been completed.</p>
								<p>All 12 Puget Sound local health jurisdictions have adopted comprehensive management plans for on-site sewage systems under the state on-site sewage rule. The management plans frame the local Operation & Maintenance programs. While the 12 local Operations & Maintenance programs have common elements, they are all uniquely designed and implemented. The Department of Health oversees the statewide on-site sewage rule and collects and interprets local data for the Puget Sound targets.</p>
								<h4 class="green">Phase in an expansion of marine recovery areas designations</h4>
								<p>DOH and LHJs have asked the Partnership to revisit and reconsider this part of the target due to concerns with its feasibility.</p>
								

								<div class="greenBarHeader">Is there progress? </div>
								<p>Part 1: On-site sewage inventory, inspection, and repair </p>
								<p>Progress toward the 2020 target (Part 1) is mixed. There have been advances in inventorying and inspecting septic systems. However, the analysis of progress on fixing failures of septic systems is pending due to lack of data. </p>
								<p>The 12 Puget Sound local health jurisdictions (LHJs) have inventoried nearly 67,000 on-site sewage systems in Marine Recovery Areas and other specially designated areas since 2011. The inventory includes septic systems with official records and that are current with inspection, systems that are overdue for an inspection, and systems that are suspected to exist but are not officially documented. Since 2011, the baseline reference year, nearly 14,000 additional septic systems were added to the inventory. The overall percentage of on-site sewage systems that have been inventoried and documented rose from 86 percent to 95 percent during this period and reflects progress toward the 2020 target of 100 percent.</p>
								<p>The percent of septic systems that have been inspected and the inspection is current increased from 32 percent to 42 percent between 2011 and 2015. Although there has been progress relative to the baseline reference, the indicator is are still far from reaching the 2020 target value of 95 percent. Working with homeowners to ensure and track ongoing regular inspections is challenging and costly. State and federal funds have provided additional resources for work on the 2020 targets.</p>
								<p>The third measure of this target is to fix all failures in the designated areas. The analysis is pending due to lack of data. The Washington State Department of Health (DOH) is working with the LHJs to standardize an approach to track and report system repairs and expected to begin reporting this measure in 2014.</p>

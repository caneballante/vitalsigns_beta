								<h4 class="green">Why is it happening?</h4>
<p>The local health jurisdictions are currently working to adapt and align their programs to fit with these ambitious regional targets.</p>
								<p>The Puget Sound Operations &amp; Maintenance (O&amp;M) programs in local health jurisdictions are inherently complex and costly to implement. They all work from the same rule requirements and core elements, but are all tailored to local conditions, budgets, and ways of doing business. They require significant planning, infrastructure, personnel, public education, political support, community buy-in, financial resources, and smart execution.</p>
									<p>	At all levels of government, funding for decentralized wastewater programs and infrastructure dramatically lags behind public investment in centralized sewer systems. State financial support for the Puget Sound O&amp;M programs has never materialized at a scale originally envisioned when the state on-site sewage and Marine Recovery Area laws were enacted. Most O&amp;M program costs are covered locally and are complemented by state and federal grants. The Washington State Department of Health (DOH) surveyed the local programs in 2014 to determine financial need. The analysis documented highly variable local expenditures ($6+ million including grants) and needs, and estimated total annual need at more than $14 million to implement current programs, and more than $24 million to carry out baseline services regionally.</p>
								<p>The targets provide a small window into the workings of the local O&amp;M programs. These programs include such diverse activities as financial lending for system repairs, code enforcement, homeowner inspection training, data management, certification of O&amp;M professionals, homeowner notification and reporting, and community outreach.</p>
								<p>&ldquo;Management&rdquo;—characterized here as O&amp;M —has long been recognized as the weak link in the widespread use of on-site sewages systems when compared to centralized sewers. The picture is gradually changing in the Puget Sound region as local O&amp;M programs take root, but it will continue to take significant investments and smart thinking to effectively design and deliver these utility-style programs and services on an ever-expanding scale.</p>
								<p>Homeowners and elected officials alike are increasingly seeing the need for and benefits of these programs. The Action Agenda and regional targets will continue to shape and guide these efforts.</p>

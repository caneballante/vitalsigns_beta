				<p>This indicator is the area of estuarine wetlands restored to tidal flooding in Puget Sound's large river deltas.</p>
				<p>This indicator tracks the amount of land returned to tidal flooding in the deltas (the landform that is formed at the mouth of a river where it meets Puget Sound) of Puget Sound's 16 major rivers (Deschutes, Dosewallips, Duckabush, Dungeness, Duwamish, Elwha, Hamma Hamma, Nisqually, Nooksack, Puyallup, Quilcene, Samish, Skagit, Skokomish, Snohomish, Stillaguamish). The recovery of tidal wetlands can be most directly tied to the provision of ecosystem services, particularly salmonid rearing. </p>
				<p>Restoration of tidal flooding is the critical act that begins the process of recovery—the restoration of fish access and sediment supply, and development of salt adapted vegetation and soils. Evidence collected to date indicates that full reconnection of tidal flows produces immediate benefits to estuary dependent organisms like juvenile salmon. More sophisticated project monitoring is underway to evaluate various sources of risk to sustained recovery of delta ecosystems. </p>
				<p>Estuary restoration projects have been planned, funded, and implemented over two decades by local, state, tribal and federal governments, non- profit organizations, conservation districts, and private parties. Project documentation has been scattered and inconsistent--mapping and survey methods are not standardized. The data shown in this report are an estimate of the area affected by restoration, obtained from project management databases, primarily the Washington State Recreation and Conservation Office’s Project Information System (PRISM), which feeds federal data sources. </p>
				Efforts are underway to standardize how river delta restoration efforts are reported and characterized, as part of river delta adaptive
				<p>management work implemented by the National Oceanic and Atmospheric Administration (NOAA), Washington State Department of Fish and Wildlife (WDFW), and other partners. The intent is to eliminate inconsistencies in how partners track progress, establish methods for detecting problems with wetland recovery, and begin to resolve known issues that may affect delta recovery and resilience over time and under climate change scenarios. </p>
				

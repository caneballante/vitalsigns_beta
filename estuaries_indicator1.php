		<p>Each project and watershed characterizes estuary restoration differently and project reporting is scattered and inconsistent. Consequently, we report these data as an approximation of actual area restored. However, initial work by the Washington Department of Fish and Wildlife (WDFW) and the National Oceanic and Atmospheric Administration (NOAA) to standardize tracking of estuary restoration in large river deltas suggests that our current acreage estimate is fairly accurate.</p>
<p>Large acreage projects have been concentrated on the Dungeness, Quilcene, Skokomish, Skagit, Nooksack, Stillaguamish, and Nisqually river deltas. In particular, work in the Quilcene, Skokomish, and Nisqually have restored almost to the full extent of historical tidal flows. </p>
<p>While substantial projects have been completed in the Nooksack, Skagit, Stillaguamish, and Snohomish estuaries, these efforts remain modest when compared to the mammoth historical extent of these river deltas. Many smaller projects have been completed in the industrialized Duwamish and Puyallup river deltas as a result of salmon recovery actions as well as Natural Resource Damage Assessments linked to Superfund cleanups. </p>
<p>Additional projects have restored estuary wetlands outside of the deltas, at natal estuaries for salmon other than Chinook. These efforts are much smaller in scale, but are significant in that migrating fish, such as threatened Hood Canal summer chum salmon, benefit from a distributed network of high quality shoreline rearing opportunities. We will ultimately benefit from developing strategies that track the protection and restoration of more distributed tidal wetland habitats.</p>
<p>Although areas restored to tidal flooding is our primary indicator, many efforts are concerned with how river flows are distributed over the delta. For example, parallel to the well publicized restoration of tidal flow in the west bank Nisqually Refuge, the Nisqually Tribe was completing efforts to restore river flooding over the east bank into Red Salmon Slough. By contrast, the Elwha dam removals have initiated the expansion of the sediment starved Elwha Delta through natural processes. Salmon recovery targets on the Skagit hinge on reconnecting Skagit river flow through a middle distributary to Fir Island marshes.</p>
<p>Roughly 1,200 acres of restoration are anticipated over the next five years. At this rate, we are not likely to reach the 2020 target. Project work is constrained by limited human resources, unpredictable capital funding flows, and social conflicts over returning low lying farmland to the tides. </p>
 

<p>Data on acres of estuarine lands where tidal flow was restored are provisional because each project characterizes estuary restoration area differently. The Partnership is working with agencies and watershed groups as part of the Delta Consortium to standardize how estuary restoration is measured and reported. 
</p>
<h5>Historic trends</h5>
<p>In Puget Sound has 16 large river-mouth estuaries: nine larger deltas drain the Cascade Mountains, and seven smaller deltas drain the Olympic Mountains. These estuaries and wetlands were a cornerstone of the Puget Sound ecosystem and served as a critical nursery for historically large populations of now-threatened Pacific salmon.</p>
<p>Over the last 150 years, the region has suffered dramatic losses of intertidal wetlands. Of the approximately 62,000 acres of mapped historical swamp and marsh, only an estimated 14,640 acres remain. The swamps of the Skagit and Snohomish alone once contained over 37,000 acres (compared to around 1,620 acres for all the Olympic deltas combined). In the most highly developed river mouth estuaries, such as the Duwamish and Puyallup rivers, estuarine habitat has been reduced to only a tiny fragment of its original extent, and is unlikely to be recovered.</p>
<p>These losses can be attributed to agricultural and industrial development. Estuaries and tidal wetlands provided an opportunity to develop flat land near water, and were diked, drained, and in some cases filled. Diking and drainage allowed for development of farms on rich fertile marsh soils, and dredging and filling enabled the construction of modern ports and industrial sites. Loss of intertidal wetlands has contributed to the decline of many species, especially Chinook and chum salmon that depend on river delta estuaries for rearing habitat.</p>
<p>In addition to direct conversion, the management and modification of our large rivers has affected delta health and resilience. Dams and reservoirs capture sediment that would otherwise build deltas. Flood defenses pipe the remaining sediments into deeper water, burying historical eelgrass meadows. The load of nutrients and toxins now in our waters have known effects on some biota, but poorly understood effects on delta ecosystems overall.  </p>
<h5>Recent trends</h5>
<p>Diverse salmon recovery and watershed restoration groups are working with the support of local, state, and federal funding to set local watershed-specific restoration targets, identify willing landowners, and work through sometimes intense local politics to restore estuarine wetland habitat. These efforts are technically complex, and often require strong partnerships in a challenging social, economic, and natural environment.</p>
<p>Habitat losses still occur, but by more subtle mechanisms than the large scale historical losses. While dredging, log rafting, nutrient loading, and toxic storm water continue, perhaps the most significant risk to existing estuarine wetlands may now come from sea level rise. Where river sediments are insufficient and dikes prevent migration landward, wetlands may ultimately be squeezed out of existence by rising tides. The predicted subsidence of diked land, rising seas, more violent storms, and increased river flooding portend a developing crisis that will define the future of river delta landscapes.</p>
<p>Advances in remote sensing technologies, current investments in long term fish monitoring, and strategic investment in delta restoration science promise to improve our understanding of the effects of habitat gains and losses and the natural dynamics of delta ecosystems, allowing us to improve our efficiency by informing where and how to restore these unique and irreplaceable landscapes. </p>

				

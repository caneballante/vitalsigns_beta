<p>Much of the floor of Puget Sound is covered with sediments—the gravel, sand, silt, and clay that has accumulated over years, decades, centuries, and even millennia. High-quality sediments support a diverse and important biological community. </p>
<p>The Sediment Quality Triad Index (SQTI) measures three aspects of sediment quality in Puget Sound. The SQTI includes: </p>
<ol><li>Chemical contamination, as described in the Sediment Chemistry Index (SCI), which is also an indicator for this Vital Sign;</li>
<li>Toxicity of the sediments to marine life, which measures the combined (synergistic) effects of those chemicals and other sediment characteristics on laboratory test organisms; and </li>
<li>Diversity of the benthos, which measures the total number of organisms and total number of species of small invertebrates living in the sediments and reflects the actual biological condition of the sediments as a response to all possible human-caused and natural stressors, whether measured or not. </li></ol>

<p>The SQTI ranges from 1-100, with higher index values indicating higher quality sediments (Table 1). </p>

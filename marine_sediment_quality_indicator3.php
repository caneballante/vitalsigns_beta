								<h4 class="aqua">Sediment Quality Triad Index</h4>
										<p>Sediment quality is a key indicator of a healthy ecosystem, and high-quality sediments support a diverse and important biological community. We monitor three aspects of sediment quality in Puget Sound, including:  1) chemical contamination - described for the SCI, above, 2) toxicity of the sediments to marine life - measures the combined (synergistic) effects of those chemicals and other sediment characteristics on laboratory test animals, and 3) diversity of the benthos – measures the total number of organisms and total number of species of the communities of small invertebrates living in the sediments, which reflects the actual biological condition of the sediments as a response to all possible human-caused and natural stressors, whether measured or not.  These three measures are known as the <em>Sediment Quality Triad.</em></p>
										<p>The Sediment Quality Triad Index (SQTI) provides a weight-of-evidence approach that combines the SCI, sediment toxicity data, and benthic invertebrate community data into a single, broad measure of sediment quality (Dutch et al. 2012).  The SQTI ranges from 1-100, with higher index values indicating higher quality sediments (Table 2). </p>

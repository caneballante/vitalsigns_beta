<img src="images/toxics_in_sediment_chart_3.png" width="800" height="708" alt="Sediment Quality Triad Index" />
<p>The Sediment Quality Triad Index (SQTI), suggests a mixed portrait of Puget Sound sediment quality. In the initial round of baseline sampling conducted between 1997 and 2011, four of eight regional areas and three of five sampled urban bays exceeded or were statistically no different from the target value of 81, indicating “unimpacted” sediments (Table 3 in Is there progress? section and Figure 3 in Latest data and maps section). The remaining four regions had somewhat lower scores, but still fell within the range characterized as “likely unimpacted” (SQTI >57-81).  The remaining two bays had much lower scores, falling within the range characterized as “possibly impacted”.</p>

<p>The SQTI scores for the re-sampled regions and bays raise a concern. Among four regions and three bays that were re-sampled from 2004-2009, SQTI scores improved in only one area—Whidbey Basin—and declined in the other six areas (Figure 3 in Latest data and maps section). This decline in SQTI values is heavily influenced by the benthic infaunal component of the SQTI calculation.  Declines in the number and types of benthic invertebrates have been seen in most of the resampled regions and bays, resulting in lower SQTI scores.  Two regions and two bays for which baseline sampling was first conducted in 2002, 2003, 2010, and 2011 also did not meet the SQTI target value due to low numbers and types of benthic invertebrates.  Declines in the benthic infauna are of concern to scientists, as these animals are an important source of food for other marine life, from bottom feeding fish to gray whales.  Problems with this resource may be magnified and resonate through higher levels of the Puget Sound food web.</p>

<h5>Trends	</h5>
<p>Despite the good and stable values seen in the Sediment Chemistry Index, and the improvements shown in this Sediment Quality Standards Index relative to their targets, the most striking feature of the sediment quality data is the apparent widespread decline in overall SQTI scores. This decline was statistically significant in two areas: Central Sound and Bainbridge Basin.</p>

<p>The lower SQTI values were driven primarily by reductions in the benthic invertebrate community measures. There appear to be large increases in the incidence and spatial extent of adversely affected benthos between the first (baseline) samples collected in the late 1990s and more recent samples. Invertebrate abundance and species richness has decreased significantly in some areas. The reasons for the decline in benthic health are not known. Decline in benthic invertebrate communities is evident in both urban and nonurban areas, with only limited correlation with changes in sediment chemistry or toxicity.</p>

<p>Since changes in the benthos aren’t closely correlated to the chemical and toxicity-related environmental parameters currently being measured, other factors must be important. Benthic invertebrate communities are affected by a complex interplay of natural and human-caused variables, and there are many environmental factors that can impact benthic invertebrate populations that aren’t measured by the SQTI. These include low dissolved oxygen, pH, sediment flux and loading, natural population cycles, and a variety of species interactions. All of these factors can have important local effects. For example, benthic communities sampled in Hood Canal in 2004 appeared to be adversely affected by very low, near-bottom dissolved oxygen concentrations (Long et al. 2007).</p>

<p>Other possible factors include the introduction of new chemicals of concern not currently monitored, and sub-lethal toxic effects such as reproductive impairment, that are not easily identified by current toxicity testing methods.</p>

<p>Over time, changes in sediment quality reflect the cumulative effects of many factors impacting the chemistry, physical processes, and biological responses of the Puget Sound ecosystem. The Sediment Quality Triad is a useful integrating measure of sediment condition, which can both explain observed effects, and help focus new inquiries on emerging problems.</p>



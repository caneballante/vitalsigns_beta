				<h5>Is the indicator making progress toward the 2020 target? </h5>
				<p> <span><img src="images/status_mixed_results.png" width="118" height="121" alt=""/>The results for this indicator are mixed.  </p>
				<p>Sediment Quality Triad Index results suggest a mixed portrait of Puget Sound sediment quality. </p>
				<ul><li>Five of eight regions and three of six urban bays sampled since 1997 currently have mean SQTI scores that statistically meet the target value of 81.  Three regions, Hood Canal, the San Juan Islands, and the Eastern Strait of Juan de Fuca, and three bays, Bainbridge Basin, Bellingham Bay, and Budd Inlet, do not meet the target value. </li>
					<li>Among eight regions and three bays that were re-sampled from 2004 – 2015, SQTI scores improved in Whidbey Basin, South Sound, the San Juan Islands, and Elliott Bay, but declined in the other eight areas. This decline in SQTI values is primarily due to declines in the number and types of benthic invertebrates, resulting in lower SQTI scores. Two bays, Bellingham Bay and Budd Inlet, for which baseline sampling was first conducted in 2010 and 2011, respectively, also did not meet the SQTI target value due to low numbers and types of benthic invertebrates.</li>
					<li>A third round of sampling was conducted in Elliott Bay in 2013, Commencement Bay in 2014, and Bainbridge Basin in 2015. On a positive note, the SQTI scores improved and exceeded the target value in Elliott Bay and Commencement Bay. As a result, the 2014 interim targets were all met.  The SQTI score for Bainbridge Basin in 2015, however, declined, fell below the target value, and did not meet the 2016 interim target.  Again, the declining score was due to lower numbers and types of benthic invertebrates.</li>
					<li>Hood Canal was not resampled in 2016, so the 2016 interim SQTI target could not be evaluated.</li></ul>
					

<p>	Although there has been no progress made since 2010, the population has been growing, albeit slowly at about 1% per year, over the longer term (1979 to 2010). This population growth trend is consistent with the 2020 target. However, trends could easily be reversed, as the Southern Resident Killer Whale population is very vulnerable to a variety of factors, making progress toward the 2020 target tenuous at best.</p>

						

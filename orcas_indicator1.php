								<!--*********************************PINK HEADER*********************************-->
								<div class="pinkBarHeader">What is the indicator? </div>
								<p>Number of Southern Resident Killer Whales.</p>
							
										<p>The Southern Resident Killer Whale population in Puget Sound is actually a large extended family, or clan, comprised of three pods: J, K, and L pods. Although they can be seen throughout the year in Puget Sound, they are most often seen during the summer, especially in Haro Strait west of San Juan Island, the Strait of Juan de Fuca, and in the Strait of Georgia near the Fraser River.</p>
										<p>Threats to Southern Resident Killer Whales include contaminants, prey availability, vessels, and noise pollution. Additional human activities, such as underwater military activities, have been identified as a potential concern for killer whales, particularly on the outer coast. This issue has not been fully evaluated. Their small population size and social structure put them at risk for a catastrophic event, such as an oil spill, or a disease outbreak, that could impact the entire population.</p>
										<p>Resident orcas were chosen as an indicator because they are top-level predators, spend a portion of the year in Puget Sound to feed and socialize, and are threatened by some of the pressures on the Sound, such as pollution and declining salmon and herring runs. Although a robust orca population is an important recovery goal both at the state and federal levels, there may be limits to how much the orca indicator can tell us about the overall health of Puget Sound. The Southern Resident Killer Whale population migrates in and out of the area, and thus is not entirely dependent on Puget Sound and its resources.</p>
							
								<!--*********************************PINK HEADER*********************************-->
								<div class="pinkBarHeader">What is the target</div>
								<h5>2020 target</h5>
								<p>By 2020, achieve an end-of-year census of 95 individual Southern Resident Killer Whales, which would represent a 1% annual average growth rate from 2010 to 2020.</p>

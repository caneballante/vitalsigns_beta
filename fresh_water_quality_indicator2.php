				<h4 class="aqua">Number of impaired waters</h4>
				<p>Although the number of impairments for rivers and streams decreased by 77 segments in 2010 - a step in the right direction - it does not mean that these segments now meet water quality standards. Instead, the change in number of impairments was largely due to the number of segments receiving approval for their water quality improvement project plans or pollution control programs. </p>
				<p>Having a plan in place removes a segment from the impairment list, but does not necessarily mean that the area has been restored or that water quality standards are being met. For example, only four segments from the 2010 list were removed from the impaired list because they met water quality standards. </p>
				<p>New data for freshwater were not reviewed in 2010; the 2012 water quality assessment will use new data and be published sometime in 2013. The number of freshwater impairments is likely to rise significantly in 2012 due to an increase in data and the number of sites assessed. Comparing the number of impairments for 2008 to 2012 will be difficult because the method used to map and count segments will change. </p>

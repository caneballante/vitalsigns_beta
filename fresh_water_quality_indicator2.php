				  <h5>This indicator is the number of impaired waters.</h5>
				  <p>When lakes and streams have a reduced ability to support native species and human uses, then they are listed as Impaired. Impaired waters are segments of streams, rivers, or lakes that do not meet the State of Washington&rsquo;s Water Quality Standards for bacteria, dissolved oxygen, temperature, toxics, or other pollutants. Cool, clean water is a key ingredient for a healthy Puget Sound. Under the Federal Clean Water Act of 1972, waters are considered impaired when they fail to meet water quality standards or minimum requirements for certain uses. Every two years, states are required to prepare a list of water bodies that do not meet water quality standards. This list is called the 303(d) list, because the process is described in Section 303(d) of the Clean Water Act. To achieve this goal, the State of Washington established water quality standards designed to protect and restore water quality for drinking, recreation, and habitat for fish and other aquatic life.</p>
				  <p>More than one segment of a river can be listed as impaired, and a single segment can be listed for more than one pollutant. Once a segment is listed as impaired, a plan must be created and implemented to control pollution or improve water quality. The effects of these restoration programs can take many years to have a positive impact.</p>
			  <p>The Washington State Department of Ecology reviews data from a variety of sources to identify impairments relative to strict standards. Each freshwater listing is a unique combination of an assessment unit (a segment of river), medium (water, tissue, or sediment), and parameter (temperature, bacteria, or dissolved oxygen). Monitoring multiple parameters at several stations along the length of shoreline typically results in multiple assessment units, each of which will have a listing developed for each monitored parameter. The possible outcomes of the water quality assessment under the Clean Water Act 303(d) section are:</p>
				  <blockquote>
				    <p>Category 1: Meets Water Quality Standards<br />
				      Category 2: Water of Concern<br />
				      Category 3: Insufficient Data<br />
				      Category 4A: Total Maximum Daily Loads have been established for the identified impairment<br />
				      Category 4B: Impaired but an alternative pollution control plan is in place<br />
				      Category 4C: Impaired by non-pollutant<br />
				      Category 5: Impaired by a pollutant or an unidentified cause</p>

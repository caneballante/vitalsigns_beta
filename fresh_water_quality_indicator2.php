				<h4 class="aqua">Number of impaired waters</h4>
				<p>Impaired waters are segments of streams, rivers, or lakes that do not meet the State of Washington’s Water Quality Standards for bacteria, dissolved oxygen, temperature, toxics, or other pollutants. Cool, clean water is a key ingredient for a healthy Puget Sound. When lakes and streams have a reduced ability to support native species and human uses, then they are listed as Impaired.</p>
				<p>The Washington State Department of Ecology reviews data from a variety of sources every four years to identify impairments. The data used to list segments as impaired must meet rigorous data quality standards as outlined in Washington’s Water Quality Policy 1-11.</p>
				<p>Under the Federal Clean Water Act of 1972, waters are considered impaired when they fail to meet water quality standards or minimum requirements for certain uses. Every two years, states are required to prepare a list of water bodies that do not meet water quality standards. This list is called the 303(d) list, because the process is described in Section 303(d) of the Clean Water Act. To achieve this goal, the State of Washington established water quality standards designed to protect and restore water quality for drinking, recreation, and habitat for fish and other aquatic life.</p>
				<p>More than one segment of a river can be listed as impaired, and a single segment can be listed for more than one pollutant. Once a segment is listed as impaired, a plan must be created and implemented to control pollution or improve water quality. The effects of these restoration programs can take many years to have a positive impact. </p>
				

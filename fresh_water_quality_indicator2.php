				<p>In the Puget Sound basin, the 2010 Water Quality Assessment showed a total of 6,957 segment and parameters combinations were assessed. A total of 1,496 river and stream segments, in 525 rivers and streams, did not meet Water Quality Standards and thus were listed as impaired.</p>
				<p>Impairments occurred in all 19 Water Resource Inventory Areas (WRIAs) in the Puget Sound basin (Figures 5 to 7). More than 60% of the total number of listings for Puget Sound rivers and streams were in five watersheds: Nooksack (296 listings), Kitsap (194), Cedar/Sammamish (181), Duwamish-Green (132), and Lower Skagit-Samish (109). For Puget Sound lakes, 52 were listed as impaired; 48% were listed for bacteria and total phosphorus, and approximately one-half were listed for toxic chemical contamination.</p>
				<p>The most frequently cited data for listing segments as impaired were bacteria (524 listings), dissolved oxygen (460), temperature (353), and pH (97). However, the largest number of segments (39%) could not be categorized because of insufficient data. Water Quality Standards include strict rules about the number of samples required to determine whether a segment is impaired or meeting standards.</p>
				<p>Segments listed as waters of concern have data that indicate a problem, but not enough data to make a determination of impairment.</p>
				<p>Sampling of streams, rivers, and lakes tends to focus in areas with known problems; therefore, not all segments have been assessed, and some impairments can be missed. Consequently, impairment data are not a complete reflection of the overall health of all streams, rivers, and lakes in Puget Sound watersheds.</p>
				<p>In addition, selection of monitoring sites is frequently constrained by funding. Monitoring efforts are split between monitoring established sites and looking for new problems. This limits the numbers of new waters that are addressed during a cycle.</p>

				<p>Since 1988, water quality has been assessed in approximately 10 percent of the waters of Washington State. The 2014 assessment report contains 10,175 listings for freshwaters in the Puget Sound region. Of these 1,694 listings (about 17 percent) are in Category 5, per the Candidate 303(d) list. An additional 741 listings also do not meet water quality standards but are in Categories 4A and 4B because they have an established cleanup plan. However, most listings (4,583) have insufficient data to determine the water quality status. These are unofficial results and are pending approval by the U.S. Environmental Protection Agency.</p>
<ul><li>To examine the change in listing rates over time, a subset of listings whose water quality status was reported on in both the 2004 and 2014 assessments was used (a total of 2,071 listings). The status of 68 listings changed category from impaired (Categories 5, 4A, or 4B) to non-impaired (Category 1). For most of these de-listings, the improvement in water quality is the result of the successful implementation of pollution control practices; in particular, efforts to reduce fecal bacteria pollution.</li>
<li>Another sign of progress is that since 2004, pollution control plans have been developed for 236 of the new or ongoing 303(d) listings in efforts to restore water quality.</li>
<li>However, a greater number of listings (392) have moved from a non-impaired status to an impaired status between assessments. Most of these new 303(d) listings are due to additional data that have improved the characterization of water quality conditions rather than indicating worsening water quality.</li>
<li>The fate of the majority of listings was unchanged and remained impaired (852). An absence of change in category between assessments may be due to following: newer data verifying no water quality change; newer data that are insufficient to justify a category change; or no new data collected since 2004 (a category carries through time until newer data justifies a category change). Data about water quality are not systematically collected and submitted for each body of water with a listing. The assessments were not designed to answer questions about the status and trends of water quality. Therefore, reporting meaningfully on the progress of this indicator is not possible at this time.</li></ul>

                                <h4 class="orange">Sound Behavior Index </h4>
                                
                                <p>Many of our common day-to-day behaviors and practices seem benign on their own, but, when multiplied by 4.5 million residents, their cumulative effects can harm Puget Sound. Reversing this trend - fostering beneficial behaviors and reducing detrimental ones - is a crucial step in Puget Sound’s recovery.</p>

  <p>The Sound Behavior Index tracks 28 specific practices that can affect water quality and aquatic habitat. The index is based on a survey conducted every two years among a statistical sample of Puget Sound residents. It asks them about specific, measurable, repetitive behaviors on their own properties.</p>

  <p>The index does not attempt to include all environmental behaviors. Instead, it focuses on those proven to affect water quality and habitat: behaviors associated with toxins, bacterial contamination, vegetation management, animal waste management, and septic system function. </p>

  <p>It distills the region’s environmental performance into a single score, which can be tracked across time. By measuring long-term shifts in behaviors and practices across the Puget Sound region, the index gives policy makers a tool to set priorities for regional and local programs. The regional index can also be broken down to the county level, for those interested in locally specific information.</p>

  <p>In time, as we gather more data through successive iterations of the surveys, we will look for trends. Increases in the index value, compared to previous iterations, will indicate increased adoption of environmentally beneficial practices across the region. Decreasing values will indicate declining adoption of these practices.
 </p>
                            <!--******************************ORANGE HEADER*********************************-->
                            <div class="orangeBarHeader">What are the targets? </div>
                                

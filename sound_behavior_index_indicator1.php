				<h5>What is the trend?</h5>
				<p>The Sound Behavior Index can detect meaningful change only in the long-term. Results from three surveys so far show that about half (11) of the behaviors improved slightly, about half (10) declined and a few (7) did not change. When combined into an index score, results indicate very little change over time. The value of the index is in detecting trends across successive surveys. More iterations of the index will produce greater confidence in any detected trends.</p>
<p>The region-wide index value for the second iteration in 2013 is 0.83, down from the 2012 baseline index value of 1. This superficially suggests change to the negative; however, detailed analysis suggests that variation between the random populations surveyed in 2012 and 2013 was the cause. Therefore, beginning in spring 2015 we changed from a random-sample to a rolling-sample to reduce this kind of variation as much as possible.</p>
                				<img src="images/SBI.png" alt="Sound Behavior Index"/>
                                <p><em>Source: Puget Sound Partnership</em></p>

              <h5>Why this is happening?</h5>
				<p>The 2012 baseline index value for the 12-county region is set at 1.0, which is a typical starting point for a new index. The sampling approach and sample sizes to establish the baseline index were necessarily exploratory in nature. The second and third iterations of the Sound Behavior Index provide opportunities to better understand the data, specifically the driving factors for change in the index and the extent to which sampling methods are influencing the results. Data collection for the third iteration is complete and we expect to have the analysis and results available by summer 2015.</p>
<p>Developing an index that is consistently measured and meaningful over time requires observation of the index and its components, and adjustment of measurement techniques as opportunities for improvement arise. Because the 2013 survey obtained responses from a new panel of randomly-selected respondents, it revealed the extent to which there can be change in responses as a result of variation in the individuals sampled versus changes in population behavior.</p>
<p>The change that occurred between 2012 and 2013 is a composite of true changes in behavior as well as differences in the characteristics of the specific sample of respondents. Although it is not possible to establish precisely the relative importance of these two sources of change, the size of the change is large given that many of the drivers of behavior (lawn and boat ownership, etc.) are unlikely to have changed materially over the course of a year.</p>
<p>One approach to address the “noisiness” of data that sampling variance creates, without changing the basic elements or approach of the SBI, is the use of a “rolling panel” survey. This approach was used to collect data for the 2015 iteration of the Sound Behavior Index. A rolling sample provides a way to isolate changes in behavior from changes in the respondents being studied by sampling from a semi-fixed panel of individuals who are queried in each survey cycle. In other words, we attempt to survey the same respondents from year to year. Because over time individuals move away, lose interest in participating, age, etc., the panel needs to be partially refreshed in each survey iteration by random draws from the greater population to remain representative.</p>


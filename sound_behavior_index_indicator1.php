								<p>The 2012 survey was the first opportunity to survey the behaviors that constitute the Sound Behavior Index. That first iteration was, therefore, exploratory in nature. The second (year 2013) and third (year 2015) iterations of the SBI provide opportunities to better interpret and understand the data, specifically the driving factors for change in the index and the extent to which sampling methods influence the results. They suggest opportunities for future refinement of the index. </p>

<p>The region-wide index value for the second iteration in 2013 is 0.83, down from the 2012 baseline index value of 1. This value superficially suggests change to the negative, however, detailed analysis suggests a large portion of that change is the result of variation between the random populations surveyed in 2012 and 2013. Therefore, beginning in spring 2015 we changed from a random-sample to a rolling-sample to reduce this kind of variation as much as possible. See “Why this is happening” below for more detail.</p>

<p>The Sound Behavior Index is based on a survey of 3,100 Puget Sound residents. The survey asks questions about 28 indicator behaviors, each of which has the potential to impact the health of Puget Sound and its tributary streams and rivers. These behaviors were selected because they are repetitive, individual or residential scale practices that can be tracked using standard survey methods and they impact water quality and aquatic health.</p>
<p>Other behaviors were considered and not used because they are: one-time actions that do not reflect ongoing behavior patterns, difficult to track with a telephone survey, expert practices that most residents cannot independently implement, emerging issues that have had little public exposure, or indirectly connected to the health of Puget Sound.</p>

<p>Should future surveys reveal improvement in environmental behaviors, the index value is expected to rise. Deterioration in future behavior should reduce the index value. As with many indices, the index value itself has no unit of measure and has no inherent meaning. The index only has value in detecting trends across successive surveys. In time, more iterations of the index will produce greater confidence in any detected trends.</p>

<p>For the results of the first survey done in 2012, <a href="images/soundbehavior_chart.png" target="_blank">click here</a>: </p>
 <img src="images/vs05_soundbehavior_2015.png" width="700" height="1264" alt=""/>
<h4 class="orange">Why this is happening?</h4>

<p>The 2012 baseline index value for the 12-county region is set at 1.0, which is a typical starting point for a new index. Since the 2012 survey was the first opportunity to survey the behaviors that constitute the SBI, the sampling approach and sample sizes were necessarily exploratory in nature. The second and third iterations of the SBI provide opportunities to better understand the data, specifically the driving factors for change in the index and the extent to which sampling methods are influencing the results. Data collection for the third iteration is complete and we expect to have the analysis and results available by summer 2015.</p>

<p>Index development processes tend to be evolutionary in nature. Developing an index that is consistently measured and meaningful over time requires observation of the index and its components, and adjustment of measurement techniques as opportunities for improvement arise. With the results of the 2013 survey in hand, we now have information to guide refinement of the survey and approach. Since the 2013 survey obtained responses from a completely new panel of randomly-selected respondents, it revealed the extent to which there can be change in responses as a result of variation in the individuals sampled versus changes in population behavior.</p>


<p>The change we see between 2012 and 2013 is a composite of true changes in behavior as well as differences in the characteristics of the specific sample of respondents. Although it is not possible to establish precisely the relative importance of these two sources of change, the size of the change is large given that many of the drivers of behavior (lawn and boat ownership, etc.) are unlikely to have changed materially over the course of a year.</p>


<p>One approach to address the “noisiness” of data that sampling variance creates, without changing the basic elements or approach of the SBI, is the use of a “rolling panel” survey. This approach was used to collect data for the 2015 iteration of the Sound Behavior Index, which is currently being analyzed. A rolling sample provides a way to isolate changes in behavior from changes in the respondents being studied by sampling from a semi-fixed panel of individuals who are queried in each survey cycle. In other words, we attempt to survey the same respondents from year to year. Because over time individuals move away, lose interest in participating, age, etc., the panel needs to be partially refreshed in each survey iteration by random draws from the greater population to remain representative. </p>


<h4 class="orange">How do we calculate the index?</h4>


<p>The Sound Behavior Index is produced using a statistical method called Polychoric Principal Components Analysis. In essence, an index value is created for each survey respondent based on his or her responses to the survey questions. If a question does not apply to a respondent then the question is scored at the most environmentally benign response. For example, a respondent that does not own a vehicle cannot answer the vehicle care questions; they cannot pollute via their non-existent vehicle, therefore they score well on those questions. The index values for all respondents are then combined to produce a regional Sound Behavior Index value.</p>

<p>The index values for respondents within each of the twelve counties can also be combined to produce county-level index values. The county index values may be used to track trends at the county scale in the same way the regional index value is used to track trends at the regional scale. In this first iteration of the index, the county values provide a baseline against which future change can be measured.</p>

<p>County index values can be used to track change over time within each county. Differences in index values between different counties should be interpreted with caution because different locations have different characteristics that influence environmental behaviors.</p>

<p>For example, a person who does not have a boat, lawn, or a septic system does not have the opportunity to pollute via those things. The portions of people who own such things varies from county to county (e.g., more boats per capita in San Juan County, more people without yards in King County, more septic systems per capita in Mason County). Therefore, some locations present a greater opportunity for environmentally harmful behaviors than other locations regardless of the motivations of their residents. One county may superficially appear to exhibit worse environmental behavior than another based on their index values. In fact, however, the opposite might be true if one considers the “opportunity factor”.</p>

<p>Regionally, respondents exhibited wide variation in their reported use of yard and garden chemicals and fertilizers, and in motor vehicle washing habits. They also showed wide variation in exterior household maintenance, shoreline vegetation maintenance, and in their treatment of native plants and weeds.</p>

<p>Much less variation is detected in septic system maintenance, livestock maintenance, use of household cleaners, and maintenance of motor vehicle leaks. In other words, across the region, residents tend to behave somewhat consistently in these behaviors. It does not mean that their behavior, although consistent, is necessarily environmentally friendly.</p>

<p>Understanding this variation, or lack thereof, may provide resource managers insight into varied strategic approaches needed to address different practices and target audiences.</p>


				<p>Because the rate of permitting new armoring under the HPA has slowed and more armoring was permitted for removal than added in 2014 and 2015, this indicator appears to make some progress toward the 2020 target. </p>
<p>More permitted armoring was gained than lost cumulatively since 2011, resulting in a net cumulative length of 1.1 miles (6,000 feet). However, in 2014 and 2015, more permitted armoring was removed than was added, a ratio that aligns well with the 2020 target.</p> 

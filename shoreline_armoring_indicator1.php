				<div class="purpBarHeader">What is the indicator? </div>
				<p>This indicator is the length of new and removed armor, and the resulting net length, (new minus removed) permitted under the Hydraulic Project Approval.</p>
<p>Shoreline armoring is the most common type of shoreline modification on Puget Sound. The term includes structures like bulkheads, seawall, and rip rap. Armoring directly alters geologic processes that supply sediment to build and maintain beaches and spits. Armoring also impact erosion patterns on nearby beaches, alter beach substrate and hydrology, and reduce the availability of large wood.</p>
<p>These physical changes to beaches can diminish the availability and condition of key shoreline habitats. Armoring can also directly impact organisms and ecological processes by burying or displacing upper beach habitat and altering the natural transition between terrestrial and aquatic ecosystems. Impacts of armoring differ from one coastal setting to another, but have been demonstrated both on Puget Sound and elsewhere to impact habitat for fish, birds, and invertebrates. On the other hand, removing armoring increases food and shelter for salmon, sand for small fish to lay eggs, and invertebrates that many species of birds eat.</p>
<p>Because of these adverse impacts on coastal processes and nearshore habitat and the positive results of removing armoring, the goal is to decrease the amount of new armoring that occurs on Puget Sound, while also seeking opportunities to remove armoring where feasible.  As our understanding of techniques to avoid impacts and consider alternatives related to armoring increases, the amount, type and location of new armoring   is changing too.</p>
<p>As new armoring is being constructed, concurrent efforts are deployed to remove armoring primarily for habitat restoration. Thus, it is the difference between new and removed armoring that is of interest to address the Partnership&rsquo;s target for 2020 specifically, reported here as the net amount of shoreline armoring. The target for this indicator is that new armoring be less than removed armoring cumulatively from 2011 to 2020.</p>
<p>Alterations to the shoreline are regulated primarily by two state laws, the Shoreline Management Act and the Hydraulic Code. Permits issued under the Hydraulic Code for in-water and shoreline construction activities include the length of added, replaced or removed armoring Washington Department of Fish and Wildlife has been tracking Hydraulic Project Approvals (HPAs) permits since YEAR. Thus, data reported here were compiled from HPAs issued by the Washington Department of Fish &amp; Wildlife.  While other data from local studies and shoreline permits under the Shoreline Management Act may have more detailed information, the HPA database is the most consistent and accessible for all of Puget Sound.  HPA projects were identified as:</p>
                <ol start="1" type="1">
                  <li>New      (previously unarmored shoreline)</li>
                  <li>Replacement      (complete replacement of existing armoring)</li>
                  <li>Removal      (removal of existing armoring without replacement) </li>
                </ol>
                <p>The Puget Sound Nearshore Ecosystem Restoration Project (PSNERP) has compiled and reported on changes to shorelines in Puget Sound over the past several decades. Data reported here on the length of shoreline and the overall amount of existing shoreline armoring in Puget Sound  is from PSNERP.</p>

				<h5>Why is this happening?</h5>
				<p>The trend in the amount of new armor permitted between 2005 and 2016 decreased, while the amount of armor removed increased. A continued positive trend in removals is likely associated with both: 1) an increased awareness of the impacts to shoreline functions caused by armor, and 2) the efforts by restoration groups to address the problem by seeking potential removal opportunities.</p>
<p>New armor, however, continues to be constructed at the rate of about 0.66 miles per year. This apparent contradiction is in part because the situations where new armoring is occurring are quite different than those where it is being removed.  New armoring is driven by a perception of risk—sometimes a very real risk—to property and safety. New armoring may occur following severe storms and landslides and may be done as an emergency measure, and therefore may not receive the scrutiny normally applied to such projects. Shorelines are inherently dynamic. Much new armoring is likely occurring on properties that were laid out or developed decades ago, without sufficient attention to erosion and flood hazards.</p>
<p>On the positive side, restoration groups appear to have become increasingly successful at obtaining funding from restoration grant programs for armor removal projects as indicated by both the scale and number of removals in recent years. In addition, public facilities such as state and county parks have become a large component of the armor removal trend. </p>
<p>Grant funds have also contributed to increased outreach to shoreline property owners, agency staff, contractors, and others who have an interest in the condition of Puget Sound shorelines. Recent studies have provided demographic information on shoreline property owners to facilitate outreach activities that resonate with these owners who have considerable influence on shoreline activities. In addition, local governments on Puget Sound are in the process of updating Shoreline Master Programs in accordance with the new state guidelines.  We expect these to reduce the amount of armoring in the future, but our current data do not reflect the potential effect of these new SMPs.  We will be looking at the data over the next few years to see if we can detect a change in armoring trends in jurisdictions with updated SMPs.</p>






						<h3><a href="#!">Importance to Puget Sound recovery</a></h3>
						<div>
								<p>At low tide, the waters of Puget Sound reveal an amazing abundance of oysters, clams, mussels, and more—a bounty unparalleled elsewhere. Gathering shellfish is a time-honored tradition for the public, and today it is an industry that supports thousands of jobs and brings millions of dollars into the region.</p>
								<p>Around Puget Sound, there are approximately 190,000 acres of classified commercial and recreational shellfish beds. However, about 36,000 acres of shellfish beds—or 19%—are closed due to pollution, most of which comes from fecal bacteria from humans, livestock, and pets. When fecal bacteria and other contaminants get into the water, they threaten the areas where these prized oysters, clams, and other bivalve shellfish grow.</p>
						</div>
						
						<!--*********************************FOLD 04*********************************-->
						<h3><a href="#!">What you can do</a></h3>
						<div>
								<ol>
										<li>Manage manure: Collect, cover and compost. If you keep livestock, follow manure management practices. Your<a href="http://scc.wa.gov/contacts/conservation-districts/"> local Conservation District</a> can provide you free technical assistance and will work with property owners to develop a waste management plan. </li>
										<li>Inspect and maintain your on-site sewage system – see see Septic System Care at <a href="http://www.pugetsoundstartshere.org/septic/index.php">Puget Sound Starts Here</a>.</li>
										<li><a href="http://www.scooppoop.org/">Pick up dog poop</a> and put it in the trash.</li>
										<li>Use <a href="http://www.skagitcounty.net/Departments/PublicWorksCleanWater/DogCleanUp.htm">porta potties</a> when near rivers.</li>
										<li>Plant and maintain native vegetation around your property. For more information on native plants, visit<a href="http://www.ecy.wa.gov/programs/sea/pugetsound/species/native.html"> Dept. of Ecology's website</a> or the <a href="http://www.wnps.org/index.html">Washington Native Plant Society</a>. </li>

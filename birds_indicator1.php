<h5>REPRODUCTIVE SUCCESS AND OTHER FACTORS INFORMING THE INDICATORS</h5>
<strong><p>Marbled murrelet</p></strong>
				<p>Although marbled murrelets spend the vast majority of its life at-sea foraging on fish, it is also dependent on forest habitats where it nests on branches of large trees. The species is federally listed as threatened. To better understand marbled murrelet decline, and to inform conservation planning to recover the species, <a href="http://www.reo.gov/monitoring/reports/murrelet/Raphael%20et%20al_2014_MAMU%20habitat%20associations_%20JMarineSyst.pdf">researchers recently evaluated how terrestrial and marine factors influenced the distribution and abundance of the murrelet in coastal waters of the U.S. </a></p>
			  <p>In 2016, <a href="https://www.fs.fed.us/pnw/pubs/pnw_gtr933.pdf">researchers also completed a 20-year review of the status and trends of murrelet populations and nesting habitat</a>. They found that the murrelet populations in California and Oregon did not exhibit a population trend while the Washington population was declining. They also found that numbers of birds on the water were positively correlated with amounts and pattern (large contiguous patches) of suitable nesting habitat, and that population trend was most strongly correlated with trend in nesting habitat although marine factors also contributed. Their results suggest that conservation of suitable nesting habitat is key to murrelet conservation, but marine factors, especially factors that contribute to murrelet prey abundance, may play a role in murrelet distribution and trend. </p>
<p><em>Rhinoceros auklet</em><br>
  Rhinoceros auklets nest in burrows on two islands in Puget Sound, namely Smith and Protection Islands in the Eastern Strait of Juan de Fuca. Peter Hodum (University of Puget Sound), Tom Good (NOAA Fisheries) and Scott Pearson (Washington Department of Fish and Wildlife) have monitored the colony on Protection Island since 2006. Results indicate that burrow occupancy in 2016 (83%) was higher than the long-term mean (72 ± 6%; Fig. 2). Hatching success in 2016 (82% of eggs laid) was similar to the long-term mean (85 ± 5.5%; Fig. 2). However, fledging success in 2016 (49%) was much lower than the long-term mean (77 ± 10%; Fig. 2). This low fledging success is the lowest ever recorded for the island including estimates from the mid-1970s.</p>
				<p>Over most years, long-term reproductive and population monitoring has been highly consistent across the decades. <a href="https://depts.washington.edu/coasst/news/breaking_news/RHAU%20Fact%20Sheet%209Aug16_2.1.pdf">The exceptionally low fledging success coincided with an unusual number of rhinoceros auklets washing up dead on the beaches in the region</a>. Although the cause of this mortality event are still under investigation, preliminary evidence suggests that bacterial infection was the primary cause of mortality.</p>
				<p>Protection Island is a globally important nesting site for rhinoceros auklets with over 35,000 occupied burrows and is managed jointly by <a href="http://americanornithologypubs.org/doi/pdf/10.1525/cond.2013.110207">US Fish and Wildlife Service and Washington Department of Fish and Wildlife</a>.</p>

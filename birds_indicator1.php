				<p>Sound-wide, the density of pigeon guillemots and rhinoceros auklet has varied from year-to-year but with no overall trend over the past 16 years. In contrast, density of the marbled murrelet population has declined by nearly 5% per year over the past 16 years (95% CI = -7.7 to –2.1). The population size of marbled murrelets in Puget Sound and the Strait of Juan de Fuca in 2016 was estimated at 4,614 birds (95% confidence interval = 2,298 – 7,570 birds). Most of the murrelet population was found in the Admiralty Inlet, San Juan Islands, and Strait of Juan de Fuca regions. Estimates of population size are not available for pigeon guillemots and rhinoceros auklets. </p>
				<p>For scoters (surf, black, and white-winged scoter species combined), the Washington Department of Fish and Wildlife developed a new data analysis and processing approach using data from 2014-2016. Estimate of trend over time using this new approach are not yet available. WDFW estimates that there were 50,920 (95% CI =50,000-52,000) scoters (black, surf, &amp; white-winged scoters) in the Salish Sea. Eighty-eight percent of scoters were observed in shallow waters (less than 20 m depth). Across the Sound and Strait, scoter densities varied by region (basin), with high abundance in the eastern end of the Strait of Juan de Fuca, and moderate abundance in the South and Central Puget Sound. Similar data are currently being compiled for the years prior to 2014 and trends for scoters are forthcoming. More information can be found in the <a href="http://www.arcgis.com/home/webmap/viewer.html?webmap=2d7eb8143c3c49679dcf8af25ee20b0a&amp;extent=-127.2413,46.3222,-119.1279,49.3197">Marine Bird Density Atlas.</a></p>

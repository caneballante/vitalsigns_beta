<p><em>Scoters</em><br>
	Three scoter species (surf, black and white-winged) over-winter in the Salish Sea. They spend about eight months of the year, with some non-breeding individuals here year-round. They are foraging, replacing their feathers and putting on weight needed to successfully reproduce in high arctic lakes in the summer. <a href="http://onlinelibrary.wiley.com/doi/10.1111/cobi.12378/abstract">Recently published research by Vilchis et al. (2015)</a> indicates that over-wintering species were three times more likely to exhibit population declines in the Salish Sea than local breeders; for scoter, between 1994 and 2010, they declined in 9% of the 67 depth/basin strata evaluted. In general, Vilchis et al (2015) found that alcids (species like the marbled murrelet and common murre) and sea ducks (species like the scoters) dominated the over-wintering bird community during the 1990s, whereas non-diving bird species and diving species with diverse diets dominated the wintering bird community in the 2000s.</p>

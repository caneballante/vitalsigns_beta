			<div> 
				<!--*********************************AQUA HEADER*********************************-->
				<div class="aquaBarHeader">What is the indicator? </div>
				<h5>The Water Quality Index</h5>
				<p> The Water Quality Index for rivers and streams combines eight measures of water quality. Expectations for four of the component measures (dissolved oxygen, pH, temperature, and fecal coliform bacteria) are tied to the State’s Water Quality Standards for protecting aquatic life and contact recreation. The other four measures (nitrogen, phosphorus, suspended sediment, and turbidity) do not have numeric standards, however they are related to general ecosystem function. Toxics are not included in the index. </p>
				<p> Index values are based on monthly monitoring at individual stations. The index values range from 1 to 100; a higher number is indicative of better water quality. However, a particular station may receive a good Water Quality Index score, and yet have water quality impaired by parameters not included in the index. Similarly, some locations may have poor Water Quality Index scores based on measures that do not have Water Quality Standards. </p>
				<p>In general, stations scoring 80 and above met expectations for water quality according to Washington State Standards for surface water quality (Chapter 173-201A WAC), scores 40 to 80 indicate water quality is marginal, and water quality at stations with scores below 40 do not meet expectations. For measures without established criteria, a score of 80 indicates conditions were better than 20% of results collected during an historical benchmark period. </p>
				<p>The Washington State Department of Ecology, King County and other jurisdictions monitor 55 long-term monitoring stations across Puget Sound watersheds. These stations are located all along rivers and streams, from the mouth of the rivers to further upstream toward the headwaters. The Puget Sound Partnership’s target data come from these 55 stations. Of the 55 long-term monitoring stations, 14 are located near the mouth of major rivers. They are used as useful measures that integrate water conditions in the uppper watershed.</p>
				
				<!--*********************************AQUA HEADER*********************************-->
				<div class="aquaBarHeader">What is the target? </div>
				<h5>2020 target</h5>
				<ul>
					<li>At least half of all monitored stations should score 80 or above on the Water Quality Index.</li>
				</ul>
				<h5>Interim targets</h5>
				<p>Interim targets have not been set for these indicators</p>
				
				<!--*********************************AQUA HEADER*********************************-->
				<div class="aquaBarHeader">Is there progress? </div>
				<p>&nbsp;</p>
				<p>There has been no progress toward the 2020 target for the Water Quality Index. Only 27% of monitored stations were at or above the target value of 80, on average, from 2009 to 2013. This percentage is a few percentage points lower compared to the baseline reference established for the 2004-2008 period (31%), but the difference is not significant. Results suggest that the water quality for rivers and streams throughout Puget Sound has essentially remained unchanged for at least the past 10 years.</p>
				<p>Five river systems – Deschutes, Nisqually, Green, Cedar, Samish rivers - show some improving trends that are significant. However, due to the slow rate of improvements in overall Puget Sound water quality trends, the Partnership’s recovery target is not likely to be reached by 2020. </p>

			<!--*********************************FOLD 03*********************************-->
			<h3><a href="#!">Progress summary and data</a></h3>
			<div> 
				<!--*********************************AQUA HEADER*********************************-->
				
				<div class="aquaBarHeader">Data</div>
				<img src="images/WQI_percentage.png" width="370" height="499" alt="Percentage of 55 monitored stations in each water quality index score categories"/>
				<p>From 2009 to 2013, 15 of the 55 monitoring stations (27%) had average Water Quality Index scores of 80 or more. This percentage is the same than the 2008-2012 average reported in 2013. Fifteen stations had averages in the 70 – 79 range; 22 had poor scores (40 – 69); and three stations had a very poor index score (< 40). Water Quality Index results for 2009-2013 indicate that only about one quarter of monitoring sites support water quality goals for conventional pollutants (toxics are not included). The majority of stations do not meet expectations for good water quality.</p>
				<img src="images/vs19_WQI_2015.png" width="836" height="532" alt=""/>
				<p>Water quality at the mouth of a river is influenced by what happens further upstream. Thus, the Water Quality Index scores at 14 river mouth monitoring stations gives us an integrated view of upstream conditions. Some river systems are consistently scoring high, i.e., they have high water quality, such as the Duckabush and Skokomish River. Other rivers consistently score low, such as the Nooksack and Puyallup rivers. Yet, scores from other rivers vary from year to year, like the Skagit and Deschutes. Stations meeting water quality goals are all in the relatively undeveloped Olympic Peninsula, except for the Snohomish River. Stations not meeting water quality goals tend to be in watersheds with more people and more agricultural development. Improvements in fecal coliform bacteria and total nitrogen index scores have also been reported (p < 0.05).</p>
				<p>None of the major rivers have had significantly declining Water Quality Index scores (p < 0.20) since 1995. Water Quality Index scores have improved significantly in the Nisqually, Deschutes, Green, Cedar and Samish systems. </p>
				<img src="images/WQI_averageannualscores_2015.png" width="700" height="556" alt=""/>
				<p>Overall, WQI scores for major rivers in the Puget Sound have been in the mid 70’s and show a slight increasing trend of 0.34 units per year (p &lt; 0.10). The variability in WQI scores has also decreased since 1995 at a rate of 6.0 units per year (p &lt; 0.05). </p>
				<p>The earliest projection for meeting the target value for the 14 major rivers would be 2025. When adjusted for differences in seasonal flows, the trend is much slower: average flow-adjusted scores of 80 are projected for 2060. Adjusting the flow accounts for the effect of flow on the parameters underlying the index. Our projection to and beyond 2020 is a best guess. Predictions are difficult due to fluctuations in drivers like the rate of population growth, global warming, and effectiveness of management activities, as well as possible long-term cycles not visible in the current 15-year dataset. For example, management tends to address the easier and more egregious problems first. As those problems get fixed, remaining problems become more difficult to correct with less effect on the water body for a given level of effort. Consequently, the rate of improvement in the index could be less, perhaps much less, than predicted by simply extending current trends.</p>
				<h4 class="aqua">Why is this happening?</h4>
				<p>In addition to some improvements in the overall score since 1995 for major rivers in Puget Sound, fecal coliform bacteria and total nitrogen index scores also improved (p&lt;0.05).  Furthermore, there are some slight reductions in mean annual stream temperature and total suspended sediment concentration on a few systems. However, Puget Sound-wide trends for other measures were not significant. </p>
				

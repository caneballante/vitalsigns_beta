								<p>Between 1991 and 2011, a cumulative total of 62,080 acres of non-federal forest land was converted to developed lands. The rate of forest loss declined fairly constantly over this 20 year period. It went from a high of 5,376 acres per year between 1991 and 1996, to a low of 1,196 between 2006 and 2011. </p>
<p>The data used to report on this Vital Sign target were based on land-cover change information for lands not in federal ownership as determined by the Landsat satellite imaging system. </p>
<p>Due to image element limitations, this approach does not capture relatively small land use changes, such as clearing for single homes or lot expansion. Therefore, only larger events (more than two acres) are reliably captured in these values.
Current work is underway to develop a more sensitive land cover change analysis using photos that have a higher resolution (1 m) than the current analysis (30 m). With these high-resolution photos, we are able to identify smaller changes and tease apart the changes occurring in areas with a complex mix of land covers. The WDFW for this project is<a href="http://wdfw.wa.gov/conservation/research/projects/aerial_imager y/index.html"> here</a>.</p>

<h4 class="purp">Why is this happening?</h4>
<p>Since 1991, the rate of forest loss has been declining fairly constantly. In Puget Sound, the amount of forest is declining, but the rate of that loss has slowed. Although the specific drivers of the decrease in forest conversion to development are not well understood, they may be tied to the economic downturn and the corresponding decrease in new development. At the same time, there are concrete efforts to manage urban growth at local and state levels. More work is needed to understand what factors, including salmon recovery efforts, land use policies, and economic growth, best explain forest conversion.</p>

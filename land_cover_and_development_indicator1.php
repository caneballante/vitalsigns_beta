								<div class="purpBarHeader">What is the indicator?								</div>
								<h4 class="purp">Forest loss, measured by the number of acres of non-federal forest land cover converted to development.</h4>
										<p>The forest loss indicator measures the loss of non-federal forested land cover to developed land cover. The indicator provides a check on our regional success in maintaining forest cover throughout the Puget Sound Basin.</p>
										<p>Forests are important for the health of Puget Sound watersheds because forested landscapes provide:</p>
										<ol>
												<li>Habitat functions that support terrestrial species</li>
												<li>Watershed functions that support freshwater systems, and</li>
												<li>Provisioning and cultural services for humans.</li>
										</ol>
										<p>Changes in forest cover are measured using National Oceanic and Atmospheric Administration (NOAA) Coastal Change analysis of Landsat satellite imagery with a 30 m resolution. The forest loss indicator tracks the conversion of forested cover, including coniferous, deciduous, and mixed forest classes, to developed land cover using four classes of development intensity. Forest cover conversion in the Puget Sound Basin has been consistently measured every four to five years since 1992; the most recent results are for the period 2006-2011. Results are reported as an annual average loss, in acres, for a five-year period.</p>
										<p>While the forest loss indicator provides a good measure of forest cover change across all Puget Sound watersheds, it underestimates forest conversion, particularly in urbanizing areas that exhibit complex mixes of land cover. Additionally, urban in-fill, or the continuing conversion of small forested parcels, often occurs at sizes too small to be captured reliably by the 30m NOAA satellite data used for this indicator. Work at the <a href="http://wdfw.wa.gov/conservation/research/projects/aerial_imagery/index.html" target="new">Washington Department of Fish and Wildlife</a> is underway to develop a more sensitive land cover change analysis based on high-resolution photography. </p>

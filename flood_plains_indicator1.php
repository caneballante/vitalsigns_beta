				<div class="purpBarHeader">What is the indicator? </div>
				<p>Floodplains are areas that occupy valley bottoms adjacent to rivers. They are dynamic landscape features, shaped by the river and formed primarily of silt, sand, and gravel left by the river when it floods or changes course.</p>
<p>Floodplains are an important part of the Puget Sound landscape, particularly in the lowlands along large rivers. They are used by people for cities, farms, and transportation. They provide habitat for fish and wildlife, which in turn play a role in human wellbeing, meeting economic, cultural, and recreational needs. They store and route water through river corridors during floods, holding some of it to recharge groundwater.</p>
<p>Protection and restoration of the services provided by floodplains is integral to the recovery of Puget Sound and the viability of Puget Sound communities. Rivers and streams around Puget Sound respond to winter storms and melting snowpack by rising and flooding low-lying areas in the floodplain. By absorbing this overflow, floodplains provide rich agricultural land; level ground that accommodates development; and refuge, food, and fresh water for a variety of species.</p>
<p>Unfortunately, human use has degraded floodplains over time. Projects are underway to better manage floodplains, restore hydrologic connectivity and natural flows, reduce flood hazards, and improve habitat for salmon.</p>
<p>Floodplain restoration is measured in three ways: </p>
                <ul>
                  <li>Documenting the footprint of floodplains associated with the 17 major rivers in Puget Sound. </li>
                  <li>Characterizing the loss of function, or degradation, of floodplains.</li>
                  <li>Tabulating the number and size of projects intended to restore floodplain function in the 17 major rivers.</li>
                </ul>
                <p><em>Extent of floodplains in Puget Sound</em><br />
                  Several available datasets have been integrated into one to describe the extent of floodplains associated with Puget Sound&rsquo;s major rivers and to characterize floodplain degradation. The methodology, along with limitations of the data, are described on pages 14 to 17 of the <em>Implementation Strategy for Puget Sound&rsquo;s Floodplain Recovery Target</em> (June 30 2016 draft). Work to improve data accuracy and the baseline estimates of degradation is ongoing.</p>
  <p><em>Degraded floodplains</em><br />
                  A degraded floodplain may be disconnected from the river by roads, levees, shoreline armoring, fill, infrastructure, or structures in the floodplain; or it may be disconnected because the character of the land changes through  resource extraction, clearing of the land or disturbing the soil, or grading to support intensive land use. In any case, inundation or channel migration processes are impeded. </p>
                <p>Degradation varies according to land cover type (such as agriculture versus commercial development) and whether an area is connected to the river&rsquo;s main stem. For example, agricultural lands tend to support more intact floodplain functions than do developed lands, and connected agricultural land retains a more functional ecosystem than does disconnected agricultural land. </p>
                <p><em>Restoration of floodplains </em><br />
                  The estimated area of projects intended to restore floodplain function is derived from the National Estuary Program On-Line Report (NEPORT). </p>

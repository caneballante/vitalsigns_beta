<h5>Did the indicator meet its 2014 interim target?</h5>
<p>Because all the regions and Commencement Bay met their target values, except for Elliott Bay, which did not improve enough, the interim targets are only partly met.</p>
<ol><li>Sediment Chemistry Index scores for all regions, and most bays either meet or exceed the target value.</li>
<li>Since 1997, all of the eight sampled regions and five of six urban bays met the Sediment Chemistry Index (SCI) target value, and index values in most areas changed little since the late 1990s.</li>
<li>Conditions have improved significantly in Elliott Bay since 1998, but not enough to reach the target value. Although the index value is slightly higher in 2013 than in 2007 in Elliott Bay, the difference is not statistically significant.</li>
<li>The value measured for Admiralty Inlet dropped significantly when resampled in 2014, but still remained above the target value.</li>
<li>The values for Commencement Bay increased slightly between 2008 and 2014, and the target value is not met.</li></ol>
<p>Given that sediment contamination generally changes very slowly, we expect most areas currently meeting the target to continue to do so through 2020 unless contaminant inputs to the areas increase. It is possible that the target may eventually be reached in Elliott Bay if conditions there continue to improve.</p>

				<div class="aquaBarHeader">What is the indicator? </div>
				<p>This indicator is the Sediment Chemistry Index.</p>
				 <p>The Sediment Chemistry Index (SCI) is one component of the Sediment Quality Triad Index (SQTI), one of the other indicators for this Vital Sign. It combines data on the concentrations of 39 of 47 chemicals for which state Sediment Quality Standards Chemical Criteria have been set into an overall index of chemical exposure (Table 1). Contaminants measured that contribute to the SCI include metals, polychlorinated biphenyls (PCBs), polynuclear aromatic hydrocarbons (PAHs), phthalates, and various other pollutants. Higher index values indicate less exposure to chemicals and thus healthier sediments (Table 1).</p>
				  <p>Results from other chemicals of concern, such as polybrominated diphenyl ethers (PBDEs), pesticides, dioxins, furans, endocrine disrupting chemicals, pharmaceuticals, personal care products, and perfluorinated chemicals, for which either no state Sediment Quality Standards have been set or measurements were not taken, are not incorporated into the SCI.</p>
				  <p>Tracking the SCI gives an indication of how concentrations of selected chemicals in marine sediments change over time, primarily in response to anthropogenic input, such as stormwater runoff and direct discharge, as well as cleanup activities and passive burial as cleaner sediments settle over older, and sometimes more contaminated, sediments.</p>

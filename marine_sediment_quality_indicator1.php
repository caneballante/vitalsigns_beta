				<p>Overall, sediments in Puget Sound appear to be in generally good condition with regard to the measured suite of chemicals. Since 1997, all of the eight sampled regions and four of five urban bays met the Sediment Chemistry Index (SCI) target, and values in most areas have changed little since the late 1990s.</p>

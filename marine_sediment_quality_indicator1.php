<h5>Did the indicator meet its 2016 interim target?</h5>
<ul>
  <li>Sediment Chemistry Index scores for all regions, and most bays continue to meet or exceed the target value.  Since 1997, all of the eight sampled regions and five of six urban bays met the SCI target value, and index values in most areas changed little since the late 1990s.</li>
  <li>SCI scores have improved incrementally and significantly in Elliott Bay since 1998, but not enough to reach the target value.  </li>
  <li>Commencement Bay SCI scores have increased over time and are above the target value.</li>
  <li>SCI scores for Bainbridge Basin increased slightly between 2009 and 2015, and the target value continues to be met.</li>
  <li>Sampling in 2016 in Bellingham Bay was delayed until 2017, so 2016 SCI target conditions cannot be evaluated until 2018.  </li>
</ul>
<p>Given that sediment contamination generally changes very slowly, we expect most areas currently meeting the target to continue to do so through 2020 unless contaminant inputs to the areas increase. It is possible that the target may eventually be reached in Elliott Bay if conditions there continue to improve.<!-- TABLE 1 -->			</p>


				<h4 class="aqua">Sediment Chemistry Index</h4>
				<p>The Sediment Chemistry Index (SCI) is one component of the Sediment Quality Triad Index. It combines data on the concentrations of a variety of chemicals into an overall index of chemical exposure (Table 1). Contaminants measured as part of the SCI include metals, polychlorinated biphenyls (PCBs), polynuclear aromatic hydrocarbons (PAHs), polybrominated diphenyl ethers (PBDEs – flame retardants), chlorinated pesticides, phthalates, some solvents, and various other pollutants. Note that analyses for newer chemicals of concern, such as dioxins, furans, endocrine disrupting chemicals, pharmaceuticals, personal care products, and perfluorinated chemicals, are not conducted as part of the Puget Sound Ecosystem Monitoring Program sediment component, and therefore not included in these Sediment Quality Dashboard Indicators.</p>
				<p>Higher index values indicate less exposure to chemicals and thus healthier sediments (Table 1). Tracking the SCI gives an indication of how concentrations of those chemicals in marine sediments change over time, primarily in response to anthropogenic input, such as stormwater runoff and direct discharge, as well as cleanup activities and passive burial as cleaner sediments settle over older, and sometimes more contaminated, sediments.</p>

			<h3><a href="#!">Importance to Puget Sound recovery</a></h3>
			<div>
				<p>Floodplains work like giant sponges. As rains increase with fall storms and snowpack melts in the mountains in spring and early summer, waters in the rivers around Puget Sound rise and flood low-lying land along the rivers and streams. In addition to absorbing this overflow, floodplains provide functions and services like refuge, food, and fresh water for a variety of species, good agricultural land through soil and habitat formation, and flat land that supports a variety of human uses.</p>
				<p>Unfortunately, the functions and services in large areas of floodplains in Puget Sound have been lost through a combination of shoreline armoring and levees, as well as residential, commercial, industrial, and agricultural development. Improving riverside and floodplain habitat is a key part of virtually all recovery plans for endangered salmon. Restoration and better management of floodplains are essential for both recovering salmon and Puget Sound.</p>
			</div>
			

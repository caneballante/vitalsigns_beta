			<h3><a href="#!">What you can do</a></h3>
			<div>
				<ol>
					<li>Keep people and property out of flood-prone areas.</li>
					<li>Protect floodplain areas that can still provide functions like flood storage and salmon habitat.</li>
					<li>Avoid building in a flood-prone area unless you plan to elevate and reinforce your home and its utilities (such as the furnace and electric panel).</li>
					<li>Find out how your insurance rates are affected by your risk of flood loss and how these rates can be reduced with good flood management planning.</li>
					<li>If you live in a floodplain area, learn about your local floodplain management plan and how to minimize your effect on the floodplain by visiting your city or county website.</li>
					<li>If you do not live in floodplain area, follow guidance on what you can do in your back yard by planting and maintaining native vegetation, and increasing water infiltration, in order to reduce water runoff. For more information on native plants, visit the <a href="http://www.ecy.wa.gov/programs/sea/pugetsound/species/native.html">Department of Ecology's website</a> or the <a href="http://www.wnps.org/index.html">Washington Native Plant Society</a>.</li>
					<li>If you have concerns about your property, consult the <a href="http://www.fema.gov/">Federal Emergency Management Agency (FEMA)</a>, who can help fund qualified activities such as home elevations, relocations, and the acquisition and removal of structures in high-risk areas.</li>

				<p>From 2013 to 2014, efforts to correct pollution sources resulted in improved water quality at Les Davies Pier at Ruston Way Waterfront Park in Pierce County;   Allyn Waterfront Park, Potlatch State Park in Mason County, and Larrabee State Park in Whatcom County. Our 2014 web report details actions that led to improved water quality: <a href="http://www.ecy.wa.gov/programs/eap/beach/AnnualSuccesses.html">http://www.ecy.wa.gov/programs/eap/beach/AnnualSuccesses.html</a>.</p>
				<p>During the 2014 swim season (Memorial Day through Labor Day),  7 of 60 of monitored beaches (12%) did not meet our recreational water quality criteria. The seven beaches that had several swimming advisories or closures during the swimming season were: </p>
				<ul type="disc">
					<li>Freeland      County Park beach in Island County </li>
					<li>Bayview      State Park beach in Skagit County</li>
					<li>Eagle      Harbor Waterfront Park beach in Kitsap County</li>
					<li>Larrabee      State Park in Whatcom County</li>
					<li>Little      Squalicum Park beach in Whatcom County</li>
					<li>Salt      Creek Recreation Area beach in Clallam County</li>
					<li>Twanoh      State Park beach in Mason County</li>
				</ul>
				<p>The beach at Freeland County Park continues to have closures due to high bacteria levels. The source of the bacteria is likely decaying seaweed (beach wrack) that collects during summer months. Summer winds cause buildup of beach wrack up to two feet deep on this north facing beach. </p>
				<p> BEACH program staff, county staff, and volunteers continue to investigate possible sources of bacteria to Bayview State Park beach in Skagit County and Little Squalicum beach in Whatcom County. </p>
				<p> Larrabee State Park beach bacterial water quality levels have improved significantly due to pollution control efforts. We hope to see continued improvement in water quality in 2015. </p>
				<p> Sewage spills have led to swimming closures at Eagle Harbor Waterfront Park beach. The public utility district is currently upgrading the sewage conveyance system along the north shore of Eagle Harbor. </p>
				<p> In previous years Salt Creek Recreation Area beach has had excellent water quality. In 2014 this beach had a number of weeks when bacteria levels were high, in the swimming closure range. Beach staff plan on conducting a shoreline survey for pollution sources at this beach in 2015.</p>
				<p> Twanoh State Park beach had several swimming closures in 2014. Additional sampling was conducted to determine the possible source(s). While results were inconclusive, Washington State Parks is planning a redesign of the swimming area in 2015. </p>
				<br />
				<img width="550" height="603" src="images/VS2_corebeaches_2015.jpg" />
				<p>Considering the same core beaches over time, no clear trend emerges in water quality at swimming beaches. Rather, results fluctuate from year-to-year. Some beaches have chronic bacteria problems that are difficult to solve. However, problems at other beaches are often temporary and quickly resolved and reflect localized problems rather than trends over all in Puget Sound water quality.</p>
				<p><strong>Why is it happening?</strong></p>
				<p>Most swimming beaches in Puget Sound have good water quality. Bacteria problems arise from faulty septic tanks, sewage spills, stormwater, and animal waste, including pet waste, farm animals, and wildlife. The source of contamination is different from beach to beach and is not always obvious. </p>
				<p>Fortunately, water quality problems at some beaches have been corrected. Sometimes progress is not made because local government lacks funding to conduct Pollution Identification and Correction (PIC) programs. Often times it is very difficult to determine the source of bacterial contamination to a beach.</p>
				<p>Also, once identified, the sources of pollution are difficult to manage and solve. For example, the likely source of fecal contamination at Larrabee State Park is raccoons that are attracted by humans using the park. Education programs discouraging park users to feed wildlife will hopefully resolve the bacteria problem.</p>
				<p>Bacteria sources can impact beaches without us knowing there is a problem.  For example a large on-site sewage system serving a number of households failed near Potlatch State Park beach. The failure was discovered because routine beach monitoring showed high bacteria levels at the beach.</p>
				

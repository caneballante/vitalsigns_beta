<p>The biomass of herring in Central and Southern Puget Sound, while variable, has shown only a slight negative trend over the past 40+ years. The population of the once largest, and genetically unique, stock of Pacific herring, the spring spawning Cherry Point stock in North Puget Sound, has declined by over 90% since 1973. The other stock that has been identified as genetically distinct, the Squaxin Pass stock in the very south of Puget Sound, has increased slightly in spawning biomass since 1973.</p>
<p>The Puget Sound herring data are characterized by broad year-to-year fluctuations, which is typical of Pacific herring and other forage fish populations, and likely reflects natural environmental and demographic variability.</p>
<p>Indeed, in Puget Sound, the bulk of the biomass of the &ldquo;all other stocks&rdquo; grouping is contributed by different stocks in different years, further implicating the role of site-specific variability.  In recent years the biomass of the Quilcene Bay stock has consistently substantially bolstered the overall estimate for the other stocks complex.</p>
<p><strong>Why is this happening</strong><strong>?</strong></p>
<p>The exact causes of the Cherry Point herring decline are unknown, but it has been variously attributed to many potential factors such as chronic pollution (e.g., PCB and PAH contaminants), oil spills, overfishing, parasites, disease, and changes in abundance of predators or prey. Changes to the natural shoreline, including nearby industrial construction and operation,  may also play a role. Finally, the extent to which food-web interactions may limit herring populations, and how such interactions are mediated by the effects of climate change, are not well understood. Further studies are needed to elucidate the effect of these possible pressures.</p>
<p>Several factors contribute to difficulties in understanding Cherry Point herring stock declines and in the trends of other herring stocks, including survey methods and exploitation rate analysis.</p>
<p>Since 1973 at Cherry Point and 1986 for the rest of the stocks, the Department of Fish &amp; Wildlife conducted a combination of Spawn Deposition surveys and Acoustic-Trawl surveys to estimate herring spawning biomass. Until 1996, the spawning biomass of the larger Puget Sound stocks typically was assessed by both methods each year, while the smaller stocks were surveyed by spawn deposition surveys every three years. Since 1996, duplicate assessment coverage was reduced and assessment for all known herring stocks was attempted each year by at least one survey method. Acoustic-Trawl surveys were discontinued in 2009 due to budget reductions  but spawn deposition surveys have been conducted annually for each stock since that time.</p>
<p>In addition to spawning biomass, biological samples used to estimate growth, mortality, and recruitment were obtained from the Acoustic-Trawl surveys. These data, which are not currently being collected, are useful for assessing the stocks&rsquo; population dynamics and capacity to meet the targets, and to understand the mechanisms driving these trends. </p>
<p>For example, for the Cherry Point and most other Puget Sound stocks, there has been a shift in the age structure of the population since the late 1970s and early 1980s toward younger fish, which probably affected their productivity and slowed  recovery. </p>
<p>As mentioned above, not all spawning grounds/stocks have been surveyed every year, especially in the 70s and 80s. To complete the time series depicted in Figure 1, data gaps were filled in with the long-term average for the stocks with missing data. Given the importance of this indicator to the recovery of the Sound, monitoring methods and analysis should be reviewed and improved to more completely and accurately report status and trends.</p>
<p>Commercial exploitation of Puget Sound herring is limited to a bait fishery, which is allowed to take up to 10% of the cumulative Puget Sound spawning biomass of Central and South Puget Sound stocks. These fisheries do not occur on the spawning grounds and target immature fish deriving from various stocks.  Landings in the past 10 years have ranged from 3% to 5% of this total and are not expected to increase significantly in the near future. This is a conservative exploitation rate, compared to a typical global exploitation rate of 20%. Although a 10% exploitation rate is precautionary, a more rigorous analysis of an appropriate exploitation rate, that accounts for current population dynamics (including age composition) and ecosystem needs (e.g., the extent of predator dependency on forage fish), is desirable to ensure sustainability of the Puget Sound herring  population at large.</p>

					


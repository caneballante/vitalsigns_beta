								<!--*********************************PINK HEADER*********************************-->
								<div class="pinkBarHeader">Is there progress? </div>
                                <p>No progress has been made and none of the 2020 target values for individual Pacific herring stocks or groups of stocks are met. Instead, the current spawning biomass of all stocks are all below both their 25-year mean baseline reference and their 2020 target values.</p>
								<p>The Cherry Point herring stock in North Puget Sound, once the largest stock in the Sound, has declined by over 90% since the earliest sampling date in 1973 and shows little sign of recovery.</p>
								<p>The Squaxin Pass and other Puget Sound stocks do not exhibit the sharp decline seen in the Cherry Point stock. Although they show broad annual fluctuations, these stocks are relatively closer to their target values. In fact, in some years, these stocks have gone above their target values. <br />
										However, these stocks are currently at biomass levels below their target values.</p>
								<p>Predicting the future condition of herring spawning biomass is difficult. Owing simply to natural fluctuations in abundance as a consequence of oceanographic and climatic parameters, the Squaxin and other Puget Sound stocks in Central and South Puget Sound may reach their respective target values again over the next  five years. However, there is no evidence to suggest that herring spawning biomass at Cherry Point will increase in the near future and reach its target value by 2020, or that the biomass of all other stocks will be sustained at or above their target values. Although potential threats have been identified, there is no consensus on which threats limit the stocks or how best to manage these stocks to achieve the 2020 target.</p>
						

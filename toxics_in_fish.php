						<h3><a href="#!">What you can do</a></h3>
						<div>
									<ol>
										<li>Don't put medicines or any chemicals down the drain because they may ultimately end up in the Sound. Find a <a href="http://www.takebackyourmeds.org/what-you-can-do/locations">drop-off location</a> in your neighborhood. </li>

										<li>Use Low Impact Development (LID) techniques to manage <a href="http://www.psp.wa.gov/stormwater.php">stormwater runoff</a> such as natural landscaping, <a href="http://raingarden.wsu.edu/">rain gardens</a>, rain barrels, green roofs and permeable paving where appropriate.</li>
<li>Dispose of oil and other auto wastes at your local <a href="https://fortress.wa.gov/ecy/hwfacilitysearch/Forms/SiteSearch.aspx">recycling location or hazardous waste collection facility</a>. Some county-based links are available <a href="https://fortress.wa.gov/ecy/recycle/UISearch/ServiceSearch.aspx">here</a>.</li>
<li>If your car is leaking oil, <em>fix it!</em></li>
<li>If you're a boater make sure fuel goes only in your tank, and not in the water. Do not fill your tank to capacity, to leave room for expansion, especially during warm weather.</li>
</ol>	

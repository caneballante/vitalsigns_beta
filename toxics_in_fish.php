						<h3><a href="#!">Importance to Puget Sound recovery</a></h3>
						<div>
								<p>Toxic pollutants in our bays, rivers, and streams can show up in the fish that live there, causing them to become diseased and posing a health threat to us when we eat the fish. Pollutants in the Puget Sound ecosystem include several important classes of chemicals including, polychlorinated biphenyls (PCBs), polybrominated diphenyls ethers (PBDEs), polycyclic aromatic hydrocarbons (PAHs), and Endocrine Disrupting Compounds (EDCs).</p>

<p>Concern over these chemicals in Puget Sound is high because they are toxic, they last for a long time in the ecosystem, and their levels increase in predators as the chemicals move up the food chain, a process called biomagnification. Measuring these pollutants in fish tissues tells us whether present-day levels are harmful to the fish or the predators that consume them and whether they are safe for us to eat.</p>

<p>Scientists have been tracking contaminant levels in Puget Sound fish since 1989 and have established threshold limits for these chemicals in fish tissues. These thresholds give us a guideline for the level of toxic chemicals that fish can tolerate, before they become diseased or show other harmful effects.
</p>

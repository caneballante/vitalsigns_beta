						<h3><a href="#!">Is there progress? Indicators and targets</a></h3>
						<div> 
								<!--*********************************AQUA HEADER*********************************-->
								<div class="aquaBarHeader">What is the indicator? </div>
								<h4 class="aqua">The levels of four types of toxic contaminants in fish</h4>

<p>The four types of contaminants in this target are:</p>
<ul><li>Polychlorinated biphenyls (PCBs)</li>
<li>Flame retardants (polybrominated diphenyl ethers, or PBDEs)</li><li>Hydrocarbons (products of petroleum or combustion; polycyclic aromatic hydrocarbons, or PAHs)</li>
<li>Endocrine disrupting compounds (typically from pharmaceuticals, personal care products, but also from a wide range of other chemicals, or EDCs).
	</li>
</ul>
<p>Each of the Toxics in Fish indicator metrics begins with a measure of the degree to which fish are exposed to toxic contaminants. In most cases this means measuring the chemicals in fish tissues, in the form of “tissue residues.” In some cases fish systems can break down or metabolize the chemicals, in which case the pollutants do not accumulate in their bodies. In these cases chemists measure “metabolites” of the chemicals, usually in the bile or blood of the fish.</p>

	<p>In order to understand the potential harm these chemicals might cause, these metrics also incorporate an understanding of the “health effects threshold” of each chemical for each species. This is the level of contamination an individual can tolerate before it experiences some health effect. The combination of knowing what contaminant levels the fish is exposed to with its tolerance for a chemical provides a guide for selecting recovery targets.</p>

	<p>In some cases it is easier to measure contaminant-induced disease or other health impairment directly. Examples of these metrics in the Toxics in Fish Indicator are PAH-related liver disease and EDC-related reproductive impairment in English sole. In these cases it is possible to observe recovery of fish health directly, after exposure to the contaminant is removed from the fish’s habitat.</p>

					
								<!--*********************************AQUA HEADER*********************************-->
								<div class="aquaBarHeader">What is the target? </div>
								<h5>2020 target</h5>
								
								<p>By 2020, contaminant levels in fish will be below health effects thresholds (i.e. levels considered harmful to fish health, or harmful to the health of people who consume them).</p>




								
								<h5>Interim target</h5>
								<p><a href="http://www.psp.wa.gov/downloads/interimtargets/Toxics in Fish Interim Targets - FINAL.pdf">Link to interim targets (PDF)</a></p>
								
								<!--*********************************AQUA HEADER*********************************-->
								<div class="aquaBarHeader">Is there progress? </div>
								<p>Progress in reducing the level of toxic contaminants in fish below their respective target value has been mixed.</p>

<p>Of the four classes of toxic chemicals being tracked and reported on, two show signs of progress (PBDEs and PAHs), one shows no change (PCBs), and for one of the four there is not enough information to determine if progress is being made (EDCs). From a fish health perspective (part 2 of the target), there has been a decline in PAH-related liver disease in English sole over the past 24 years.
</p>
							
										<p>Making progress toward 2020 targets requires identifying which chemicals are most problematic, and then controlling their sources or cleaning up pollutants that have accumulated in the environment. In some cases we can expect a significant time-lag between source control and the desired result of reducing the exposure of organisms to contaminants.
</p>

<h3>Part 1: Contaminant levels</h3>

<h4 class="aqua">Polychlorinated biphenyls (PCBs)</h4>

<p>The danger of PCBs in the environment was recognized, and production banned more than 30 years ago. PCB levels in Puget Sound fish today are probably 10 times lower than they were in the 1970s, but they have not declined in the past 20 years. PCB levels in most recent samples are high enough to trigger Department of Health consumption advisories for Chinook salmon and other species, and are probably still high enough to harm fish health. </p>

<h4 class="aqua">Flame retardants (polybrominated diphenyls ethers, or PBDEs)</h4>
<p>Some progress toward 2020 targets for PBDEs has been made. The danger of flame retardants was recognized relatively recently, and source controls have been imposed. These include a legislated ban on the use of certain PBDE compounds and voluntary reduction in production of other compounds by industry. Although it is unclear whether these actions were responsible, PBDEs have been declining in one monitored species, Pacific herring, from Central and North Puget Sound.</p>

<h4 class="aqua">Hydrocarbons (products of petroleum or combustion; polycyclic  aromatic hydrocarbons, or PAHs)</h4>
<p>Progress related to hydrocarbons (PAHs) has been mixed. This is probably related to the huge range of sources for these compounds (they come from petroleum, and from burning fossil fuels), and the difficulty in controlling such pervasive sources. Exposure of English sole to PAHs has dropped significantly in Elliott Bay, resulting in a recovery of their health (see “Disease” below). PAH exposure in Pacific herring remains moderately high in most locations.</p>

<h4 class="aqua">Endocrine disrupting compounds (EDCs)</h4>
<p>Not enough monitoring has been conducted yet to fully evaluate progress towards the target of reducing EDCs. These chemicals originate from a huge range of sources including pharmaceuticals, personal care products, plastics, other industrial, agricultural or household products, and some of the chemicals described above. Many of these chemicals can be introduced to aquatic systems via wastewater. 
</p>


								

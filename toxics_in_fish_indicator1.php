<p>The Indicator metrics provided in this summary simplify a highly complex relationship between exposure of organisms to pollutants, and the effects such exposure might have on their health. Toxic contaminants in Puget Sound are found in fish throughout the ecosystem – not just in urban areas, and not just in bottom-dwelling fish. In addition, many contaminants accumulate in fish as they age. Some of these “bioaccumulative” contaminants also move up the food chain, increasing to high concentrations in apex predators. It is important to interpret data with reference to where the fish live, where they were sampled, their age, and their position in Puget Sound’s food web. </p> 

<h4 class="aqua">Polychlorinated biphenyls (PCBs)</h4>
<p>PCBs exceeded health effects thresholds or have been identified as a risk to seafood consumers in recent years for (1) urban English sole, (2) adult Chinook salmon returning to Puget Sound rivers, (3) juvenile Chinook salmon in Puget Sound or its river mouths, and (4) Pacific herring in Southern and Central Puget Sound. There has been no significant decline in PCBs in these species for the period monitored. However, adult coho salmon returning to Puget Sound rivers were below thresholds.</p>

<h4 class="aqua">Flame retardants (polybrominated diphenyls ethers, or PBDEs)</h4>
<p>Evaluation of PBDEs is challenging because health effects thresholds are not yet available for some species. However, it appears that in most species levels are at or below obvious, immediate concern for most areas. In addition, PBDE levels appear to be declining in Pacific herring from Central and South Puget Sound. </p>

<h4 class="aqua">Hydrocarbons (products of petroleum or combustion; polycyclic aromatic hydrocarbons, or PAHs)</h4>
<p>PAHs are tracked in fish by measuring byproducts (metabolites) of the compounds in their body fluids (in Pacific herring), or by measuring liver disease caused by PAH exposure (in English sole). PAHs levels in herring, a water-column species, from Central and South Puget Sound are similar to those of some urban English sole, a bottom-dwelling species. PAH levels in both species from these areas are cause for some concern. However PAH-related liver disease has declined to near background levels in one urban area (Elliott Bay).</p>

<h4 class="aqua">Endocrine disrupting compounds (EDCs)</h4>
<p>Endocrine disrupting compounds (EDCs) are chemicals that alter the normal hormonal system of fish, often resulting in problems related to growth or reproduction. EDCs have been evaluated in two species, English sole (adults) and Chinook salmon (juveniles). EDC-related feminization of male English sole was observed at five of six sampled locations, and in juvenile Chinook salmon from three of four sampled locations.
</p>

			<h3><a href="#!">Importance to Puget Sound recovery</a></h3>
			<div>
				<p>Every time we visit the beach, fish, or dig clams in Puget Sound, we rely on good water quality. Marine water quality in much of Puget Sound is poorer than we would like, especially in areas where the circulation of water is restricted.</p>
				<p>The marine waters of Puget Sound are affected by many different factors including weather and climate, inflow from rivers and streams, discharges from wastewater treatment plants and industries, off-shore ocean conditions, storm-water runoff, and even ground water.</p>
				<p>Excess pollution can force beach closures and shellfish harvesting restrictions, and may cause excessive algae blooms that eventually deplete oxygen levels leading to fish kills.</p>
			</div>
			
			<!--*********************************FOLD 04*********************************-->
			<h3><a href="#!">What you can do</a></h3>
			<div>
				<ol>
					<li>Try to avoid using fertilizers. If fertilizer must be used, choose organic or time-released fertilizers with low levels of phosphorus and nitrogen, and follow the directions on the label. </li>
					<li>Instead of fertilizer, use compost to augment your soil and fertilize your plants. Also, compost vegetation and other yard waste; do not throw it over the bank into a waterway or shoreline.   See <a href="http://www.ecy.wa.gov/programs/swfa/organics/soil.html#home">Washington Department of Ecology's composting site</a> for more information.</li>
					<li>Plant and maintain native vegetation around your property. Leave a buffer of native vegetation to uptake nutrients and fertilizers before it reaches the lakes, streams of Puget Sound. For more information on native plants, visit the <a href="http://www.ecy.wa.gov/programs/sea/pugetsound/species/native.html">Dept. of Ecology's website</a> or the <a href="http://www.wnps.org/index.html">Washington Native Plant Society</a>.</li>
					<li>Use Low Impact Development (LID) techniques to manage <a href="http://www.psp.wa.gov/stormwater.php">stormwater runoff</a> such as natural landscaping, <a href="http://raingarden.wsu.edu/">rain gardens</a>, rain barrels, green roofs and permeable paving where appropriate.</li>
					<li><a href="http://pugetsoundstartshere.org/in-your-local-area/get-involved/">Volunteer in your area</a> to work with a group helping to reduce marine water pollution. </li>
					<li>Inspect and maintain your on-site sewage system - see Septic System Care at <a href="http://www.pugetsoundstartshere.org/septic/index.php">Puget Sound Starts Here</a>.</li>
				</ol>

				<p>From 2015 to 2016, efforts by the Skagit County Pollution Identification and Correction (PIC) program to correct pollution sources resulted in a significant improvement in water quality at Bay View State Park.<br />
During the 2016 swim season (Memorial Day through Labor Day), 5 of 61 of monitored beaches (8%) did not meet our recreational water quality criteria. The five beaches that had more than one exceedance of the swimming standard during the swimming season were: </p>
                <ul type="disc">
                  <li>Edmonds      Underwater Park in Snohomish County</li>
                  <li>Freeland      County Park in Island County</li>
                  <li>Pomeroy      Park-Manchester Beach in Kitsap County</li>
                  <li>Saltwater      State Park in King County</li>
                  <li>Sunnyside      Beach Park in Pierce County</li>
                </ul>
                <p>In previous years Edmonds Underwater Park beach has had good water quality. In 2016 this beach had two weeks when bacteria levels were high, in the swimming closure range. Beach staff conducted an investigation at this beach and met with Snohomish County Health and City of Edmonds to discuss the beach closures.</p>
                  <p>The beach at Freeland County Park continues to have closures due to high bacteria levels. The source of the bacteria is likely decaying seaweed (beach wrack) that collects during summer months. Summer winds cause buildup of beach wrack up to two feet deep on this north facing beach. </p>
                  <p>Pomeroy Park-Manchester Beach, Saltwater State Park and Sunnyside Beach generally have good water quality.  These beaches had two weeks of high levels of bacteria, but there was no conclusive reason for the high levels.</p>
                  <p>Considering the same core beaches over time, no clear trend emerges in water quality at swimming beaches. Rather, results fluctuate from year-to-year. Some beaches have chronic bacteria problems that are difficult to solve. However, problems at other beaches are often temporary and quickly resolved and reflect localized problems rather than trends over all in Puget Sound water quality.</p>
<h5>Why is it happening?</h5>
				<p>Most swimming beaches in Puget Sound have good water quality. Bacteria problems arise from faulty septic tanks, sewage spills, stormwater, and animal waste, including pet waste, farm animals, and wildlife. The source of contamination is different from beach to beach and is not always obvious. </p>
<p>Fortunately, water quality problems at some beaches have been corrected. Sometimes progress is not made because local government lacks funding to conduct Pollution Identification and Correction (PIC) programs. Often times it is very difficult to determine the source of bacterial contamination to a beach. Even when sources of pollution are identified they can be difficult to manage or solve.</p>
<p>Bacteria sources can impact beaches without us knowing there is a problem.  For example when on-site sewage system serving households fail that are located near beaches. </p>
				

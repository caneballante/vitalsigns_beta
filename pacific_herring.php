						<h3><a href="#!">Importance to Puget Sound recovery</a></h3>
						<div>
								<p>Pacific herring are one of the most abundant forage fish species in Puget Sound. Together with a few other small schooling fish species herring play a unique role in the food web: they are an essential source of food for larger fish, seabirds, and marine mammals, and as such, transfer energy from their plankton prey to these higher-level consumers.</p>
								<p>Because they are a vital component of the marine food web, Pacific herring are one key indicator of the overall health of Puget Sound. Herring stocks require clean water and natural shorelines, so their continued survival depends on maintaining links between nearshore and open-water habitats.</p>
						</div>
						
						
